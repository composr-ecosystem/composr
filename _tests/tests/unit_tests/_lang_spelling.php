                    if (in_array($path, array(
                        'themes/default/javascript/jquery.js',
                        'themes/default/javascript/jquery_ui.js',
                        'themes/default/javascript/jquery_autocomplete.js',
                        'themes/default/javascript/plupload.js',
                        'themes/default/javascript/widget_color.js',
                        'themes/default/javascript/widget_date.js',
                        'themes/default/javascript/xsl_mopup.js',
                        'themes/default/javascript/select2.js',
                        'themes/default/javascript/sound.js',
                        'themes/default/javascript/jwplayer.js',
                        'themes/default/javascript/modernizr.js',
                        'themes/default/css/jquery_ui.css',
                        'themes/default/css/widget_date.css',
                        'themes/default/css/widget_color.css',
                        'themes/default/css/widget_select2.css',
                        'themes/default/css/widget_plupload.css',
                        'themes/default/css/skitter.css',
                        'themes/default/css_custom/mediaelementplayer.css',
                        'data/editarea/edit_area_compressor.php', // TODO: Remove in v11
                        'themes/default/javascript_custom/unslider.js',
                        'themes/default/javascript_custom/columns.js',
                        'themes/default/javascript_custom/mediaelement-and-player.js',
                        'themes/default/javascript_custom/openid.js', // TODO: Remove in v11
                        'themes/default/javascript_custom/skitter.js',
                        'themes/default/javascript_custom/jquery_flip.js',
                        'themes/default/javascript_custom/sortable_tables.js',
                        'sources_custom/geshi.php',
                        'sources_custom/openid.php',
                        'sources_custom/twitter.php',
                        'sources_custom/hooks/modules/chat_bots/trickstr.php',
                    ))) {
                        continue;
                    }

                    $_c = '';
                    $matches = array();

                    $num_matches = preg_match_all('#//(.*)#', $c, $matches);
                    for ($i = 0; $i < $num_matches; $i++) {
                        $_c .= ' ' . $matches[1][$i];
                    }

                    $num_matches = preg_match_all('#/\*(.*)\*/#Um', $c, $matches);
                    for ($i = 0; $i < $num_matches; $i++) {
                        $_c .= ' ' . $matches[1][$i];
                    }

                    $c = $_c;
                }

                foreach (array('tt', 'code', 'codebox') as $tag) { // Strip whole tags
                    $c = preg_replace('#\[' . $tag . '[^\[\]]*\].*\[/' . $tag . '\]#Us', '', $c);
                }

                $c = str_replace('_', '-', $c); // Underscores as dashes

                $c = preg_replace('#\w+://[^\s]*#', '', $c); // Strip URLs

                $c = preg_replace('#\[[a-z]+#', '', $c); // Strip opening tag
                $c = preg_replace('#\[/[a-z]+\]#', '', $c); // Strip closing tag

                $matches = array();
                $num_matches = preg_match_all('#([a-z][a-z0-9\']+)#i', $c, $matches);

                for ($i = 0; $i < $num_matches; $i++) {
                    $word = strtolower($matches[1][$i]);

                    if (strlen($word) > 25) { // No really long 'words'
                        continue;
                    }

                    if (preg_match('#\d#', $word) != 0) { // No 'words' can actually have numbers in
                        continue;
                    }

                    $word = rtrim($word, "'"); // Strip back group possession apostrophe
                    $word = preg_replace('#\'s$#', '', $word); // Strip back possession structure

                    $words[$word] = $path;
                }
            }
        }

        ksort($words, SORT_NATURAL | SORT_FLAG_CASE);

        foreach ($words as $word => $path) {
            $result = pspell_check($pspell_link, $word);
            $this->assertTrue($result, 'Misspelling: ' . $word . ' (' . $path . ')');
        }
    }
