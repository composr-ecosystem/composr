                $exceptions = array_merge(list_untouchable_third_party_files(), [
                ]);
                if (in_array($path, $exceptions)) {
                    continue;
                }

                $ext = get_file_extension($path);
                if ($ext != $_ext) {
                    continue;
                }

                if (($this->only !== null) && ($this->only != $path)) {
                    continue;
                }

                if ($path == 'data_custom/errorlog.php') {
                    continue;
                }

                if ($this->debug) {
                    var_dump($path);
                }

                // Load and pre-process into plain text
                $c = cms_file_get_contents_safe(get_file_base() . '/' . $path);
                switch ($ext) {
                    case 'ini':
                        $c = str_replace('\n', ' ', $c);
                        $c = clean_simple_html_for_spellcheck($c);
                        break;

                    case 'htm':
                    case 'html':
                        $c = clean_simple_html_for_spellcheck($c);
                        break;

                    case 'md':
                    case 'txt':
                        if (in_array($path, [
                            'docs/pages/comcode_custom/EN/tut_addon_index.txt',
                            'text/EN/synonyms.txt',
                            'docs/LICENSE.md',
                            'docs/THANKS.md',
                            'docs/CODE_OF_CONDUCT.md',
                        ])) {
                            continue 2;
                        }

                        $c = preg_replace('#\[tt.*\[/tt\]#Us', '', $c);
                        $c = preg_replace('#<kbd.*</kbd>#Us', '', $c);

                        $c = clean_comcode_for_spellcheck($c);

                        break;

                    case 'js':
                    case 'css':
                    case 'php':
                        // We just want the code comments...

                        $c = preg_replace('#https?://[^\s\'"]*#', '', $c);

                        $_c = '';
                        $matches = [];

                        if ($ext != 'css') {
                            $num_matches = preg_match_all('#[^:]//(.*)#', $c, $matches);
                            for ($i = 0; $i < $num_matches; $i++) {
                                $_c .= ' ' . $matches[1][$i];
                            }
                        }

                        $num_matches = preg_match_all('#/\*(.*)\*/#Um', $c, $matches);
                        for ($i = 0; $i < $num_matches; $i++) {
                            $_c .= ' ' . $matches[1][$i];
                        }

                        $_c = preg_replace('#\w*_\w*#', '', $_c); // Not useful for checking code comments, we're often just referencing code terms
                        $_c = preg_replace('#\$\w+#', '', $_c); // Not useful for checking code comments, we're often just referencing variables

                        $c = $_c;

                        break;
                }

                $c = preg_replace('#`[^`\n]*`#', '', $c); // Protected

                $misspellings = run_spellcheck($c, null, false/*would be too slow and is not needed*/, false, false);
                foreach (array_keys($misspellings) as $word) {
                    $this->assertTrue(false, 'Misspelling: ' . $word . ' (' . $path . ')');
                }

                if ($ext != 'ini') {
                    $manual_check_ob->check($this, $path, null, $c, false);
                }
            }
        }

        // Now check addon descriptions...

        $addons = find_all_hook_obs('systems', 'addon_registry', 'Hook_addon_registry_');
        foreach ($addons as $addon_name => $ob) {
            if (($this->only !== null) && ($this->only != $addon_name) && ($this->only != '(addons)')) {
                continue;
            }

            $this->assertTrue(method_exists($ob, 'get_description'), 'Missing get_description for ' . $addon_name);

            $c = $ob->get_description();

            $c = clean_comcode_for_spellcheck($c);

            $misspellings = run_spellcheck($c, null, false/*would be too slow and is not needed*/, false, false);
            foreach (array_keys($misspellings) as $word) {
                $this->assertTrue(false, 'Misspelling: ' . $word . ' (' . $addon_name . ' addon description)');
            }
        }

        spellchecker_shutdown();
    }
