            'NAME' => do_lang_tempcode('FIELD_FILTERS'),
            'DESCRIPTION' => do_lang_tempcode('DOC_FIELD_FILTERS'),
        ]));
        $categories_tpl->attach(do_template('INDEX_SCREEN_FANCIER_ENTRY', [
