    {
        $posts = $this->get_markers();
        if (empty($posts)) {
            warn_exit(do_lang_tempcode('NO_MARKERS_SELECTED'), false, false, 400);
        }

        $to_forum_id = null;
        $to_topic_id = post_param_integer('select_topic_id', null);
