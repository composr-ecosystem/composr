                    warn_exit(do_lang_tempcode('CANNOT_CATEGORISE_INVITED'));
                }
                $GLOBALS['FORUM_DB']->query_update('f_topics', [$t => $category], ['id' => $topic_id], '', 1);
            }
        }
        return $this->redirect_to_forum('CATEGORISE_PTS', null);
    }

