        }

        if (addon_installed('content_reviews')) {
            content_review_set('catalogue_category', strval($category_id));
        }

        $this->donext_category_id = $category_id;
        $this->donext_catalogue_name = $catalogue_name;

        return [strval($category_id), null];
    }

