    public function edit_actualisation(string $_id) : ?object
    {
        $id = intval($_id);
        $cat = post_param_string('cat', fractional_edit() ? STRING_MAGIC_NULL : 'root');

        if ($cat != STRING_MAGIC_NULL) {
