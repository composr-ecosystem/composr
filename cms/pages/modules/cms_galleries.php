    /**
     * The UI to import into a gallery.
     *
     * @return Tempcode The UI
     */
    public function _import() : object
    {
        $cat = get_param_string('name', 'root');
        check_privilege('mass_import'/*Not currently scoped to categories, ['galleries', $cat]*/);

        if (substr($cat, 0, 7) != 'member_') {
            $test = $GLOBALS['SITE_DB']->query_select_value_if_there('galleries', 'name', ['name' => $cat]);
            if ($test === null) {
                warn_exit(do_lang_tempcode('MISSING_RESOURCE', 'gallery'));
            }
        }

        require_lang('trackbacks');

        // To choose to batch import from multiple attached files
        $post_url = build_url(['page' => '_SELF', 'type' => '__import', 'cat' => $cat, 'uploading' => 1, 'redirect' => protect_url_parameter(get_param_string('redirect', null, INPUT_FILTER_URL_INTERNAL))], '_SELF');
        $fields = new Tempcode();
        $supported = get_option('valid_images') . ',' . get_allowed_video_file_types();
        $fields->attach(form_input_upload_multi(do_lang_tempcode('UPLOAD'), do_lang_tempcode('DESCRIPTION_ARCHIVE_MEDIA', escape_html(str_replace(',', ', ', $supported))), 'file', true, null, null, true, str_replace(' ', '', $supported)));
        $fields->attach(form_input_line(do_lang_tempcode('TITLE'), (get_option('gallery_media_title_required') == '2') ? do_lang_tempcode('DESCRIPTION_GALLERY_IMPORT_TITLE_REQUIRED') : do_lang_tempcode('DESCRIPTION_GALLERY_IMPORT_TITLE'), 'set_title', '', false/*When required, title can be left blank to auto-generate from file names or EXIF*/));
        $hidden = new Tempcode();
        handle_max_file_size($hidden);

        if ($this->has_at_least_one_watermark($cat)) {
            $fields->attach(form_input_tick(do_lang_tempcode('WATERMARK'), do_lang_tempcode('DESCRIPTION_WATERMARK'), 'watermark', true));
        }

        // Feedback
        require_code('feedback2');
        $fields->attach(feedback_fields($this->content_type, true, true, true, false, ''));
        // Privacy
        if (addon_installed('content_privacy')) {
            require_code('content_privacy2');
            $fields->attach(get_privacy_form_fields(null));
        }
        // Max upload limit
        require_code('files2');
        $max = floatval(get_max_file_size()) / floatval(1024 * 1024);
        if ($max < 30.0) {
            $text = get_maximum_upload_message($max);
        } else {
            $text = new Tempcode();
        }
        // Show form
        $hidden->attach(form_input_hidden('test', '1'));
        $form = do_template('FORM', [
            '_GUID' => '05de2bc134ed0b1ab703ac8bfbfabce4',
            'TABINDEX' => strval(get_form_field_tabindex()),
            'FIELDS' => $fields,
            'SUBMIT_ICON' => 'admin/import',
            'SUBMIT_NAME' => do_lang_tempcode('BATCH_IMPORT_ARCHIVE_CONTENTS'),
            'URL' => $post_url,
            'TEXT' => $text,
            'HIDDEN' => $hidden,
        ]);

        // Orphaned upload form
        // To choose to batch import what already exists in gallery directory, but is orphaned
        $orphaned_content = new Tempcode();
        if ((has_actual_page_access(get_member(), 'admin_cleanup')) && ($GLOBALS['SITE_DB']->query_select_value('images', 'COUNT(*)') + $GLOBALS['SITE_DB']->query_select_value('videos', 'COUNT(*)') < 4000)) {
            require_code('files');
            $there = [];
            $_dir = opendir(get_custom_file_base() . '/uploads/galleries/');
            while (false !== ($file = readdir($_dir))) {
                if ((get_file_extension($file) != 'vtt') && (!should_ignore_file($file, IGNORE_ACCESS_CONTROLLERS)) && (!is_dir(get_custom_file_base() . '/uploads/galleries/' . $file)) && ((is_image($file, IMAGE_CRITERIA_WEBSAFE, has_privilege(get_member(), 'comcode_dangerous'))) || (is_video($file, has_privilege(get_member(), 'comcode_dangerous'))))) {
                    $there[$file] = filemtime(get_custom_file_base() . '/uploads/galleries/' . $file);
                }
            }
            closedir($_dir);
            $_dir = @opendir(get_custom_file_base() . '/uploads/galleries/' . filter_naughty($cat));
            if ($_dir !== false) {
                while (false !== ($file = readdir($_dir))) {
                    if ((!should_ignore_file($file, IGNORE_ACCESS_CONTROLLERS)) && (!is_dir(get_custom_file_base() . '/uploads/galleries/' . $cat . '/' . $file)) && ((is_image($file, IMAGE_CRITERIA_WEBSAFE, has_privilege(get_member(), 'comcode_dangerous'))) || (is_video($file, has_privilege(get_member(), 'comcode_dangerous'))))) {
                        $there[$cat . '/' . $file] = filemtime(get_custom_file_base() . '/uploads/galleries/' . $cat . '/' . $file);
                    }
                }
                closedir($_dir);
            }
            if (!empty($there)) {
                cms_mb_asort($there, SORT_FLAG_CASE | SORT_NATURAL);
                $test1 = collapse_1d_complexity('url', $GLOBALS['SITE_DB']->query_select('images', ['url']));
                $test2 = collapse_1d_complexity('url', $GLOBALS['SITE_DB']->query_select('videos', ['url']));
                arsort($there);
                $temp_keys = array_keys($there);
                $last_time = $there[$temp_keys[0]];
                foreach ($there as $file => $time) {
                    if (!is_string($file)) {
                        $file = strval($file);
                    }

                    if ((!in_array(cms_rawurlrecode('uploads/galleries/' . str_replace('%2F', '/', rawurlencode($file))), $test1)) && (!in_array(cms_rawurlrecode('uploads/galleries/' . str_replace('%2F', '/', rawurlencode($file))), $test2))) {
                        $orphaned_content->attach(form_input_list_entry($file, ($time >= $last_time - 60 * 60 * 3) || (strpos($file, '/') !== false), $file));
                    }
                }
            }
            $add_url = build_url(['page' => '_SELF', 'type' => '_orphaned'], '_SELF');
            if (!$orphaned_content->is_empty()) {
                $fields_2 = new Tempcode();
                $hidden_2 = new Tempcode();

                $hidden_2->attach(form_input_hidden('ss_cat', $cat));
                $fields_2->attach(form_input_multi_list(do_lang_tempcode('ENTRIES'), '', 'ss_files', $orphaned_content));
                $fields_2->attach(form_input_line(do_lang_tempcode('TITLE'), do_lang_tempcode('DESCRIPTION_GALLERY_IMPORT_TITLE'), 'ss_set_title', '', get_option('gallery_media_title_required') == '2'));
                if ($this->has_at_least_one_watermark($cat)) {
                    $fields_2->attach(form_input_various_ticks([[do_lang_tempcode('WATERMARK'), 'ss_watermark', true, '']], '', null, do_lang_tempcode('OPTIONS')));
                }
                $radios = new Tempcode();
                $radios->attach(form_input_radio_entry('orphan_action', 'add', true, do_lang_tempcode('ADD_GALLERY_SELECTION')));
                $radios->attach(form_input_radio_entry('orphan_action', 'delete', false, do_lang_tempcode('DELETE_SELECTION')));
                $fields_2->attach(form_input_radio(do_lang_tempcode('ACTION'), '', 'type', $radios, true));
                // Feedback
                require_code('feedback2');
                $fields_2->attach(feedback_fields($this->content_type, true, true, true, false, '', null, false, true, true, 'ss_'));
                // Privacy
                if (addon_installed('content_privacy')) {
                    require_code('content_privacy2');
                    $fields_2->attach(get_privacy_form_fields(null, null, true, 'ss_'));
                }

                require_code('syndication');
                $fields_2->attach(get_syndication_option_fields('image'));

                $form2 = do_template('FORM', [
                    '_GUID' => '79c9fd4f29197460f08443bf2ffdf8b2',
                    'SECONDARY_FORM' => true,
                    'TABINDEX' => strval(get_form_field_tabindex()),
                    'FIELDS' => $fields_2,
                    'SUBMIT_ICON' => 'admin/import',
                    'SUBMIT_NAME' => do_lang_tempcode('PROCEED'),
                    'URL' => $add_url,
                    'TEXT' => '',
                    'HIDDEN' => $hidden_2,
                ]);
            } else {
                $form2 = new Tempcode();
            }
        } else {
            $form2 = new Tempcode();
        }

        return do_template('GALLERY_IMPORT_SCREEN', ['_GUID' => '607c819ff751268294e5e590a0d41533', 'TITLE' => $this->title, 'FORM2' => $form2, 'FORM' => $form]);
    }

    /**
     * The actualiser to import multiple uploaded files into a gallery.
     *
     * @return Tempcode The UI
     */
    public function __import() : object
    {
        $cat = get_param_string('cat');

        require_code('files2');

        check_privilege('mass_import'/*Not currently scoped to categories, ['galleries', $cat]*/);

        post_param_string('test'); // To pick up on max file size exceeded errors
        $set_title = post_param_string('set_title', '');
