    /**
     * Module pre-run function. Allows us to know metadata for <head> before we start streaming output.
     *
     * @param  boolean $top_level Whether this is running at the top level, prior to having sub-objects called
     * @param  ?ID_TEXT $type The screen type to consider for metadata purposes (null: read from environment)
     * @return ?Tempcode Tempcode indicating some kind of exceptional output (null: none)
     */
    public function pre_run(bool $top_level = true, ?string $type = null) : ?object
    {
        $error_msg = new Tempcode();
        if (!addon_installed__messaged('galleries', $error_msg)) {
            return $error_msg;
        }

        $this->cat_crud_module = class_exists('Mx_cms_galleries_cat') ? new Mx_cms_galleries_cat() : new Module_cms_galleries_cat();
        $this->alt_crud_module = class_exists('Mx_cms_galleries_alt') ? new Mx_cms_galleries_alt() : new Module_cms_galleries_alt();
        $GLOBALS['MODULE_CMS_GALLERIES'] = $this;

        $type = get_param_string('type', 'browse');

        require_lang('galleries');

        inform_non_canonical_parameter('cat');
        inform_non_canonical_parameter('validated');
        inform_non_canonical_parameter('parent_id');

        if ($type == 'import') {
            inform_non_canonical_parameter('member_id');
        }

        if ($type == '_import') {
            breadcrumb_set_parents([['_SELF:_SELF:import', do_lang_tempcode('GALLERY_IMPORT')]]);
            breadcrumb_set_self(do_lang_tempcode('UPLOAD'));
        }

        if ($type == '__import') {
            $cat = get_param_string('cat');
            breadcrumb_set_parents([['_SELF:_SELF:import', do_lang_tempcode('GALLERY_IMPORT')], ['_SELF:_SELF:_import:name=' . $cat, do_lang_tempcode('DETAILS')]]);
            breadcrumb_set_self(do_lang_tempcode('DONE'));
        }

        if (($type == 'orphaned')) {
            $cat = post_param_string('ss_cat');
            breadcrumb_set_parents([['_SELF:_SELF:import', do_lang_tempcode('GALLERY_IMPORT')], ['_SELF:_SELF:_import:name=' . $cat, do_lang_tempcode('DETAILS')]]);
            breadcrumb_set_self(do_lang_tempcode('DONE'));
        }

        if ($type == 'add') {
            if (has_privilege(get_member(), 'mass_import', 'cms_galleries')) {
                $cat = get_param_string('cat', null);
                $mass_url = build_url(['page' => '_SELF', 'type' => 'import', 'cat' => $cat], '_SELF');
                attach_message(do_lang_tempcode('CAN_MASS_UPLOAD', escape_html($mass_url->evaluate())), 'inform', true);
            }
        }

        if ($type == 'import' || $type == 'orphaned') {
            $this->title = get_screen_title('GALLERY_IMPORT');
        }

        if ($type == '_import') {
            $cat = get_param_string('name', 'root');
            $this->title = get_screen_title('_GALLERY_IMPORT', true, [escape_html($cat)]);
        }

        if ($type == '__import') {
            $cat = get_param_string('cat');
            $this->title = get_screen_title('_GALLERY_IMPORT', true, [escape_html($cat)]);
        }

        if ($type == 'predefined_content') {
        }

        if ($type == '_predefined_content') {
            breadcrumb_set_parents([['_SELF:_SELF:browse', do_lang_tempcode('MANAGE_GALLERIES')], ['_SELF:_SELF:predefined_content', do_lang_tempcode('PREDEFINED_CONTENT')]]);
            breadcrumb_set_self(do_lang_tempcode('DONE'));
        }

        if ($type == 'predefined_content' || $type == '_predefined_content') {
            $this->title = get_screen_title('PREDEFINED_CONTENT');
        }

        set_helper_panel_tutorial('tut_galleries');

        return parent::pre_run($top_level);
    }

    /**
     * Standard crud_module run_start.
     *
     * @param  ID_TEXT $type The type of module execution
     * @return Tempcode The output of the run
     */
    public function run_start(string $type) : object
    {
        require_code('galleries');
        require_code('galleries2');
        require_code('uploads');
        require_css('galleries');
        require_javascript('galleries');
        require_lang('dearchive');
        require_code('urls2');
        require_code('images');

        $this->alt_crud_module->add_text = new Tempcode();

        $cat = get_param_string('cat', '');

        if ($cat != '') {
            if (strpos($type, '_other') !== false) { // Video
                $remaining = $this->alt_crud_module->check_videos_allowed($cat, true);
                if ($remaining !== null) {
                    $this->alt_crud_module->add_text->attach(paragraph(do_lang_tempcode('X_ENTRIES_REMAINING', escape_html(integer_format($remaining, 0)))));
                }
            } elseif (strpos($type, '_category') === false) { // Image
                $remaining = $this->check_images_allowed($cat, true);
                if ($remaining !== null) {
                    $this->add_text = paragraph(do_lang_tempcode('X_ENTRIES_REMAINING', escape_html(integer_format($remaining, 0))));
                }
            }
        }
        if (get_option('allow_audio_videos') == '1') {
            $this->alt_crud_module->add_text->attach(paragraph(do_lang_tempcode('ALSO_AUDIO')));
        }

        if ($type == 'add_category') {
            $this->cat_crud_module->js_function_calls[] = 'moduleCmsGalleriesRunStartAddCategory';
        }

        // Decide what to do
        if ($type == 'browse') {
            return $this->browse();
        }
        if ($type == 'import') {
            return $this->import();
        }
        if ($type == '_import') {
            return $this->_import();
        }
        if ($type == '__import') {
            return $this->__import();
        }
        if ($type == '_orphaned') {
            return $this->_orphaned();
        }
        if ($type == 'predefined_content') {
            return $this->predefined_content();
        }
        if ($type == '_predefined_content') {
            return $this->_predefined_content();
        }

        return new Tempcode();
    }

