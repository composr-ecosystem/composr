        define('MAX_STACK_TRACE_VALUE_LENGTH', (get_param_integer('keep_fatalistic', 0) == 2) ? 10000 : 300);
    }

    /** Whether we want errors to result in simple text responses. Useful for AJAX scripts.
     *
