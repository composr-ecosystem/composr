/**
 * Block class.
 */
class Block_main_staff_stats_graph
{
    /**
     * Find details of the block.
     *
     * @return ?array Map of block info (null: block is disabled)
     */
    public function info()
    {
        $info = [];
        $info['author'] = 'Chris Graham';
        $info['organisation'] = 'ocProducts';
        $info['hacked_by'] = null;
        $info['hack_version'] = null;
        $info['version'] = 1;
        $info['locked'] = true;
        $info['parameters'] = ['param', 'pivot'];
        return $info;
    }

    /**
     * Find caching details for the block.
     *
     * @return ?array Map of cache details (cache_on and ttl) (null: block is disabled)
     */
    public function caching_environment()
    {
        $info = [];
        $info['cache_on'] = <<<'PHP'
        $map
PHP;
        $info['ttl'] = 60 * 60;
        return $info;
    }

    /**
     * Execute the block.
     *
     * @param  array $map A map of parameters
     * @return Tempcode The result of execution
     */
    public function run($map)
    {
        if (!addon_installed('stats')) {
            return do_template('RED_ALERT', ['TEXT' => do_lang_tempcode('MISSING_ADDON', escape_html('stats'))]);
        }

        require_code('stats');
        require_code('zones2');

        $graph_name = empty($map['param']) ? 'total_views' : $map['param'];

        $standard = get_standard_block_parameters();

        $filters = [];
        foreach ($map as $key => $val) {
            if (($key != 'block') && ($key != 'param') && (!in_array($key, $standard))) {
                $filters[$graph_name . '__' . $key] = $val;
            }
        }
        $pivot_field_name = 'pivot'; // FUDGE: This is not a documented parameter so we need to reference it indirectly to cheat automated tests
        $pivot = empty($map[$pivot_field_name]) ? null : $map[$pivot_field_name];

        return stats_generate_graph($graph_name, $filters, $pivot);
    }
}
