    /**
     * Get an emoticon chooser template.
     *
     * @param  string $field_name The ID of the form field the emoticon chooser adds to
     * @return Tempcode The emoticon chooser template
     */
    public function get_emoticon_chooser(string $field_name = 'post') : object
    {
        require_code('comcode_compiler');
        push_db_scope_check(false);
        $emoticons = $GLOBALS['SITE_DB']->query_select('f_emoticons', ['*'], ['e_relevance_level' => 0], 'ORDER BY e_code');
        pop_db_scope_check();
        $em = new Tempcode();
        foreach ($emoticons as $emo) {
            $code = $emo['e_code'];

            $em->attach(do_template('EMOTICON_CLICK_CODE', ['_GUID' => '0b51492b6e170db4466be74fdf312260', 'FIELD_NAME' => $field_name, 'CODE' => $code, 'IMAGE' => apply_emoticons($code)]));
        }

        return $em;
    }

