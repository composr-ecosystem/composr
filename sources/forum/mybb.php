    /**
     * Find out if the given member ID is banned.
     *
     * @param  MEMBER $member_id The member ID
     * @param  ?ID_TEXT $reasoned_ban Ban reasoning returned by reference (null: none)
     * @return boolean Whether the member is banned
     */
    public function is_banned(int $member_id, ?string &$reasoned_ban = null) : bool
    {
        $rows = $this->db->query('SELECT * FROM ' . $this->db->get_table_prefix() . 'banned WHERE uid=' . strval($member_id));
        if (empty($rows)) {
            return false; // The member was never banned
        }

        $ban_till = null;
        foreach ($rows as $row) {
            // Permanent ban; set the reason and exit immediately
            if (empty($row['lifted'])) {
                $reasoned_ban = $row['reason'];
                return true;
            }

            // Check for temporary bans that are still active; we want the reason for the longest ban in the event multiple temporary bans exist
            if ((($ban_till === null) || ($row['lifted'] > $ban_till)) && ($row['lifted'] > time())) {
                $reasoned_ban = $row['reason'];
                $ban_till = $row['lifted'];
            }
        }

        return ($ban_till !== null);
    }

