    /**
     * Try and log in using a member cookie.
     * Should only be called if the cookie exists.
     *
     * @return ?array A map of 'id' and 'error'. If 'id' is null, an error occurred and 'error' is set (null: no cookie)
     */
    public function authorise_cookie_login() : ?array
    {
        if ((!isset($_COOKIE[get_member_cookie()])) || (!isset($_COOKIE[get_pass_cookie()]))) {
            return null;
        }

        $username = $_COOKIE[get_member_cookie()];
        $password_hashed = $_COOKIE[get_pass_cookie()];

        return $this->_authorise_login($username, null, $password_hashed, true);
    }

    /**
     * Find if the given member ID and password is valid. If username is null, then the member ID is used instead.
     *
     * @param  ?SHORT_TEXT $username The member username (null: use $member_id)
     * @param  ?MEMBER $member_id The member ID (null: use $username)
     * @param  string $password_raw The raw password
     * @return array A map of 'id' and 'error'. If 'id' is null, an error occurred and 'error' is set
     */
    public function authorise_login(?string $username, ?int $member_id, string $password_raw) : array
    {
        return $this->_authorise_login($username, $member_id, $password_raw, false);
    }

    /**
     * Find if the given member ID and password is valid. If username is null, then the member ID is used instead.
     * All authorisation, cookies, and form-logins, are passed through this function.
     *
     * @param  ?SHORT_TEXT $username The member username (null: use $member_id)
     * @param  ?MEMBER $member_id The member ID (null: use $username)
     * @param  string $password_mixed If $cookie_login is true then this is the value of the password cookie, otherwise it's the password the user tried to log in with
     * @param  boolean $cookie_login Whether this is a cookie login, determines how the hashed password is treated for the value passed in
     * @return array A map of 'id' and 'error'. If 'id' is null, an error occurred and 'error' is set
     */
    protected function _authorise_login(?string $username, ?int $member_id, string $password_mixed, bool $cookie_login = false) : array
    {
        $out = [];
        $out['id'] = null;

        if ($member_id === null) {
            $rows = $this->db->query_select('members', ['*'], ['member_name' => $username], '', 1);
            if (array_key_exists(0, $rows)) {
                $this->MEMBER_ROWS_CACHED[$rows[0]['id_member']] = $rows[0];
            } else {
                $rows = $this->db->query_select('members', ['*'], ['real_name' => $username], '', 1);
                if (array_key_exists(0, $rows)) {
                    $this->MEMBER_ROWS_CACHED[$rows[0]['id_member']] = $rows[0];
                }
            }
        } else {
            $rows = [];
            $rows[0] = $this->get_member_row($member_id);
        }

        if (!array_key_exists(0, $rows) || $rows[0] === null) { // All hands to lifeboats
            $out['error'] = do_lang_tempcode((get_option('login_error_secrecy') == '1') ? 'MEMBER_INVALID_LOGIN' : '_MEMBER_NO_EXIST', $username);
            return $out;
        }
        $row = $rows[0];
        if ($this->is_banned($row['id_member'])) { // All hands to the guns
            $out['error'] = do_lang_tempcode('YOU_ARE_BANNED');
            return $out;
        }

        // Verify password below
        if (substr($row['passwd'], 0, 4) == '$2y$') { // SMF 2 password
            $GLOBALS['SMF_PW_STYLE'] = 2.0;
            if ($cookie_login) {
                // TODO: Cookie login probably does not work
                $cookie_hash = $this->cookie_hash_salt($row['passwd'], $row['password_salt']);
                $passes = hash_equals($cookie_hash, $password_mixed);
            } else {
                $passes = password_verify(cms_strtolower_ascii($username) . $password_mixed, $row['passwd']);
            }
        } else { // SMF 1 and 1.1 password
            $GLOBALS['SMF_PW_STYLE'] = array_key_exists('pm_ignore_list', $row) ? 1.1 : 1.0;
            $password_hashed = $cookie_login ? $password_mixed : $this->_legacy_password_hash($password_mixed, $row['passwd']);
            $bits = explode('::', $password_hashed);
            if (!array_key_exists(1, $bits)) {
                $bits[1] = $bits[0];
            }
            if ($cookie_login) {
                if ($GLOBALS['SMF_PW_STYLE'] == 1.1) {
                    $passes = hash_equals(sha1($row['passwd'] . $row['password_salt']), $bits[1]);
                } else {
                    $passes = hash_equals($this->_legacy_password_hash($row['passwd'], 'ys', true), $bits[0]);
                }
            } else {
                if ($GLOBALS['SMF_PW_STYLE'] == 1.1) {
                    $passes = hash_equals($row['passwd'], $bits[1]);
                } else {
                    $passes = hash_equals($row['passwd'], $bits[0]);
                }
            }
        }

        if (!$passes) {
            $out['error'] = do_lang_tempcode((get_option('login_error_secrecy') == '1') ? 'MEMBER_INVALID_LOGIN' : 'MEMBER_BAD_PASSWORD');
            return $out;
        }

        if (substr(get_member_cookie(), 0, 5) != 'cms__') {
            require_code('users_active_actions');
            cms_eatcookie('PHPSESSID');
        }

        $out['id'] = $row['id_member'];

        return $out;
    }

    /**
     * LEGACY: SMF hashing for SMF 1 and 1.1.
     *
     * @param  string $password_raw The value to hash
     * @param  string $key Special key to hash with
     * @param  boolean $smf1_only Whether to just use original SMF hashing
     * @return string The hashed data
     * @ignore
     */
    protected function _legacy_password_hash(string $password_raw, string $key, bool $smf1_only = false) : string
    {
        $key = cms_strtolower_ascii($key);
        $new_key = str_pad((strlen($key) <= 64) ? $key : pack('H*', md5($key)), 64, chr(0x00));

        $a = md5(($new_key ^ str_repeat(chr(0x5c), 64)) . pack('H*', md5(($new_key ^ str_repeat(chr(0x36), 64)) . $password_raw))); // SMF 1.0 style
        if ($smf1_only) {
            return $a;
        }
        $b = sha1($key . $password_raw); // SMF 1.1 style

        return $a . '::' . $b;
    }

    /**
     * Hashes password with salt and authentication secret for use in the SMF 2 cookie.
     *
     * @param  string $password The password
     * @param  string $salt The salt
     * @return string The hashed password
     */
    protected function cookie_hash_salt(string $password, string $salt) : string
    {
        if (!function_exists('hash_hmac')) {
            warn_exit(do_lang_tempcode('INTERNAL_ERROR'));
        }

        // Append the salt to get a user-specific authentication secret.
        $secret_key = get_value('smf_auth_secret') . $salt;

        // Now use that to generate an HMAC of the password.
        return hash_hmac('sha512', $password, $secret_key);
    }

