/**
 * Switch Conversr to run over the local site-DB connector. Useful when importing and our forum driver is actually connected to a forum other than Conversr.
 */
function cns_over_local()
{
    $GLOBALS['MSN_DB'] = $GLOBALS['FORUM_DB'];
    $GLOBALS['FORUM_DB'] = $GLOBALS['SITE_DB'];
}

/**
 * Undo cns_over_local.
 */
function cns_over_msn()
{
    $GLOBALS['FORUM_DB'] = $GLOBALS['MSN_DB'];
    $GLOBALS['MSN_DB'] = null;
}

/**
 * Returns the NEW ID of an imported old ID, for the specified importation type. Whether it returns null or gives an error message depends on $fail_ok.
 *
 * @param  ID_TEXT $type An importation type code, from those Composr has defined (E.g. 'download', 'news', ...)
 * @param  string $id_old The source (old, original) ID of the mapping
 * @param  boolean $fail_ok If it is okay to fail to find a mapping
 * @return ?AUTO_LINK The new ID (null: not found)
 */
function import_id_remap_get(string $type, string $id_old, bool $fail_ok = false) : ?int
{
    static $remap_cache = [];
    if ((array_key_exists($type, $remap_cache)) && (array_key_exists($id_old, $remap_cache[$type]))) {
        return $remap_cache[$type][$id_old];
    }

    $value = $GLOBALS['SITE_DB']->query_select_value_if_there('import_id_remap', 'id_new', ['id_session' => get_session_id(), 'id_type' => $type, 'id_old' => $id_old]);
    if ($value === null) {
        if ($fail_ok) {
            return null;
        }
        warn_exit(do_lang_tempcode('IMPORT_NOT_IMPORTED', escape_html($type), escape_html($id_old)));
    }
    $remap_cache[$type][$id_old] = $value;
    return $value;
}

/**
 * Check to see if the given ID of the given type has been imported (if it has a mapping).
 *
 * @param  ID_TEXT $type An importation type code, from those Composr has defined
 * @param  string $id_old The source (old, original) ID of the mapping
 * @return boolean Whether it has been imported
 */
function import_check_if_imported(string $type, string $id_old) : bool
{
    send_http_output_ping();

    $test = $GLOBALS['SITE_DB']->query_select_value_if_there('import_id_remap', 'id_new', ['id_session' => get_session_id(), 'id_type' => $type, 'id_old' => $id_old]);
    return $test !== null;
}

/**
 * Set the NEW ID for an imported old ID, which also tacitly indicates completion of importing an item of some type of content. This mapping (old ID to new ID) may be used later for importing related content that requires the new identifier. import_id_remap_get is the inverse of this function.
 *
 * @param  ID_TEXT $type An importation type code, from those Composr has defined
 * @param  string $id_old The source (old, original) ID of the mapping
 * @param  AUTO_LINK $id_new The destination (new) ID of the mapping
 */
function import_id_remap_put(string $type, string $id_old, int $id_new)
{
    $GLOBALS['SITE_DB']->query_insert('import_id_remap', ['id_session' => get_session_id(), 'id_type' => $type, 'id_old' => $id_old, 'id_new' => $id_new]);
}

/**
 * Add a word to the word-filter.
 *
 * @param  SHORT_TEXT $word Word to add to the word-filter
 * @param  SHORT_TEXT $replacement Replacement (blank: block entirely)
 * @param  ?ID_TEXT $match_type The WORDFILTER_MATCH_TYPE_* to use (null: default to WORDFILTER_MATCH_TYPE_FULL)
 */
function add_wordfilter_word(string $word, string $replacement = '', ?string $match_type = null)
{
    require_code('wordfilter');

    if ($match_type === null) {
        $match_type = WORDFILTER_MATCH_TYPE_FULL;
    }
    $test = $GLOBALS['SITE_DB']->query_select_value_if_there('wordfilter', 'word', ['word' => $word]);
    if ($test === null) {
        $GLOBALS['SITE_DB']->query_insert('wordfilter', ['word' => $word, 'w_replacement' => $replacement, 'w_match_type' => $match_type]);
    }
}

/**
 * Detect if we have reached our timerout, more accurately than set_time_limit could.
 */
function i_timed_refresh()
{
    static $time = null;
    if ($time === null) {
        $time = time();
    }
    global $I_REFRESH_TIME;
    if (time() >= $time + $I_REFRESH_TIME) {
        i_force_refresh();
    }
}

/**
 * Force a page refresh due to maximum execution timeout / tick-function determined timeout.
 */
function i_force_refresh()
{
    if (array_key_exists('I_REFRESH_URL', $GLOBALS)) {
        if ((strpos($GLOBALS['I_REFRESH_URL'], "\n") !== false) || (strpos($GLOBALS['I_REFRESH_URL'], "\r") !== false)) {
            log_hack_attack_and_exit('HEADER_SPLIT_HACK');
        }

        global $I_REFRESH_URL, $I_REFRESH_TIME;

        $post = build_keep_post_fields([], true);
        $refresh = do_template('JS_REFRESH', ['FORM_NAME' => 'redir_form']);

        $title = get_screen_title('IMPORT');
        $url = $I_REFRESH_URL;
        foreach ($GLOBALS as $key => $val) {
            if (preg_match('#^JUMPSTART_#', $key) != 0) {
                $url = preg_replace('#&' . preg_quote($key, '#') . '=\d+#', '', $url);
                $url .= '&' . $key . '=' . strval($val);
            }
        }
        $middle = do_template('REDIRECT_POST_METHOD_SCREEN', ['REFRESH' => $refresh, 'TITLE' => $title, 'TEXT' => do_lang_tempcode('REFRESH_TIMEOUT_REACHED', strval($I_REFRESH_TIME)), 'URL' => $url, 'POST' => $post]);

        $echo = globalise($middle, null, '', true);
        $echo->evaluate_echo();
        exit();
    }
}

