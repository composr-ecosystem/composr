/**
 * Prepare a numeric value for display in a graph.
 *
 * @param  mixed $value Value
 * @return string Value for chart.js
 */
function number_raw_string($value)
{
    if (is_integer($value)) {
        return strval($value);
    }
    if (is_float($value)) {
        return float_to_raw_string($value, 2, true);
    }
    return '0';
}
