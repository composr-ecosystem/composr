/**
 * Hook class.
 */
class Hook_config_reorganise_uploads
{
    /**
     * Gets the details relating to the config option.
     *
     * @return ?array The details (null: disabled)
     */
    public function get_details() : ?array
    {
        return [
            'human_name' => 'REORGANISE_UPLOADS',
            'type' => 'list',
            'category' => 'FEATURE',
            'group' => 'ADVANCED',
            'explanation' => 'CONFIG_OPTION_reorganise_uploads',
            'shared_hosting_restricted' => '0',
            'list_options' => '0|1|2',
            'required' => true,
            'public' => false,
            'addon' => 'core_configuration',
        ];
    }

    /**
     * Gets the default value for the config option.
     *
     * @return ?string The default value (null: option is disabled)
     */
    public function get_default() : ?string
    {
        return '2';
    }

    /**
     * Code to run after the option is saved.
     *
     * @param  string $new_value The new value
     */
    public function postsave_handler(string $new_value)
    {
        require_code('tasks');
        call_user_func_array__long_task(do_lang('config:REORGANISE_UPLOADS'), null, 'reorganise_uploads');
    }
}
