/**
 * Hook class.
 */
class Hook_config_join_declarations
{
    /**
     * Gets the details relating to the config option.
     *
     * @return ?array The details (null: disabled)
     */
    public function get_details() : ?array
    {
        return [
            'human_name' => 'JOIN_DECLARATIONS',
            'type' => 'transtext',
            'category' => 'USERS',
            'group' => 'JOINING',
            'explanation' => 'CONFIG_OPTION_join_declarations',
            'shared_hosting_restricted' => '0',
            'list_options' => '',
            'order_in_category_group' => 202,
            'required' => true,
            'public' => false,
            'addon' => 'core_cns',
        ];
    }

    /**
     * Gets the default value for the config option.
     *
     * @return ?string The default value (null: option is disabled)
     */
    public function get_default() : ?string
    {
        require_lang('cns_config');
        require_code('temporal');
        return do_lang('I_AGREE_RULES') . "\n" . do_lang('I_AGREE_PRIVACY') . "\n" . do_lang('I_AGREE_UPDATES', escape_html(get_timezoned_date(time(), false)));
    }
}
