/**
 * Hook class.
 */
class Hook_config_google_analytics
{
    /**
     * Gets the details relating to the config option.
     *
     * @return array The details
     */
    public function get_details() : array
    {
        return [
            'human_name' => 'GOOGLE_ANALYTICS',
            'type' => 'line',
            'category' => 'SITE',
            'group' => 'LOGGING',
            'explanation' => 'CONFIG_OPTION_google_analytics',
            'shared_hosting_restricted' => '0',
            'list_options' => '',
            'order_in_category_group' => 5,
            'required' => false,
            'public' => true,
            'addon' => 'core_configuration',

            'maintenance_code' => 'google_analytics',
        ];
    }

    /**
     * Gets the default value for the config option.
     *
     * @return ?string The default value (null: option is disabled)
     */
    public function get_default() : ?string
    {
        return '';
    }

    /**
     * Code to run after the option is saved, if the value was changed or we are not formally setting it.
     *
     * @param  string $new_value The new value
     */
    public function postsave_handler(string $new_value)
    {
        require_code('caches3');
        regenerate_trusted_sites_cache();
    }
}
