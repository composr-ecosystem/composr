/**
 * Hook class.
 */
class Hook_config_shipping_density
{
    /**
     * Gets the details relating to the config option.
     *
     * @return ?array The details (null: disabled)
     */
    public function get_details()
    {
        return [
            'human_name' => 'SHIPPING_DENSITY',
            'type' => 'float',
            'category' => 'ECOMMERCE',
            'group' => 'SHIPPING',
            'explanation' => 'CONFIG_OPTION_shipping_density',
            'shared_hosting_restricted' => '1',
            'list_options' => '',
            'order_in_category_group' => 7,
            'required' => true,

            'public' => false,

            'addon' => 'ecommerce',
        ];
    }

    /**
     * Gets the default value for the config option.
     *
     * @return ?string The default value (null: option is disabled)
     */
    public function get_default()
    {
        if (!addon_installed('ecommerce')) {
            return null;
        }

        require_code('locations');
        if (geolocate_ip() == 'US') {
            return '139.0'; // 139 in3/lb (https://en.wikipedia.org/wiki/Dimensional_weight)
        }
        return '5000.0'; // 5000 cm3/kg (https://en.wikipedia.org/wiki/Dimensional_weight)
    }
}
