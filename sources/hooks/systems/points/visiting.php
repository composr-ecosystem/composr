/**
 * Hook class.
 */
class Hook_points_visiting
{
    /**
     * Get points earned for visiting the website; some of these will probably have been spent already.
     *
     * @param  MEMBER $member_id The ID of the member we are getting points for
     * @param  ?TIME $timestamp Time to get for (null: now)
     * @param  array $point_info The map containing the members point info (fields as enumerated in description) from point_info()
     * @return integer the number of points the member has
     */
    public function total_points(int $member_id, ?int $timestamp, array $point_info) : int
    {
        $points_gained_visiting = isset($point_info['points_gained_visiting']) ? $point_info['points_gained_visiting'] : 0;
        $points_visiting = intval(get_option('points_per_daily_visit'));

        if ($timestamp !== null) {
            $points_gained_visiting -= $GLOBALS['SITE_DB']->query_value_if_there('SELECT COUNT(*) FROM ' . get_table_prefix() . 'daily_visits WHERE d_date_and_time>' . strval($timestamp) . ' AND d_member_id=' . strval($member_id));

            if ($points_gained_visiting < 0) {
                $points_gained_visiting = 0;
            }
        }

        return $points_gained_visiting * $points_visiting;
    }

    /**
     * Calculate points earned to be displayed on POINTS_PROFILE.tpl.
     *
     * @param  MEMBER $member_id_of The ID of the member who is being viewed
     * @param  ?MEMBER $member_id_viewing The ID of the member who is doing the viewing (null: current member)
     * @param  array $point_info The map containing the members point info (fields as enumerated in description) from point_info()
     * @return ?array Point record map containing LABEL, COUNT, POINTS_EACH, and POINTS_TOTAL for use in POINTS_PROFILE.tpl. (null: addon disabled)
     */
    public function points_profile(int $member_id_of, ?int $member_id_viewing, array $point_info) : ?array
    {
        $points_gained_visiting = array_key_exists('points_gained_visiting', $point_info) ? $point_info['points_gained_visiting'] : 0;
        $points_per_daily_visit = intval(get_option('points_per_daily_visit'));

        return [
            'label' => do_lang('COUNT_VISITS'),
            'count' => $points_gained_visiting,
            'points_each' => $points_per_daily_visit,
            'points_total' => $points_gained_visiting * $points_per_daily_visit
        ];
    }
}
