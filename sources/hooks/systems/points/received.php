/**
 * Hook class.
 */
class Hook_points_received
{
    /**
     * Get number of points given as gift transaction; some of these will probably have been spent already.
     *
     * @param  MEMBER $member_id The ID of the member we are getting points for
     * @param  ?TIME $timestamp Time to get for (null: now)
     * @param  array $point_info The map containing the members point info (fields as enumerated in description) from point_info()
     * @return integer the number of points the member has
     */
    public function total_points(int $member_id, ?int $timestamp, array $point_info) : int
    {
        $points_received = isset($point_info['points_received']) ? $point_info['points_received'] : 0;

        if ($timestamp !== null) {
            // Most reliable way
            $_points_received = $GLOBALS['SITE_DB']->query('SELECT SUM(amount_gift_points) AS _amount_gift_points,SUM(amount_points) AS _amount_points FROM ' . get_table_prefix() . 'points_ledger WHERE date_and_time>' . strval($timestamp) . ' AND recipient_id=' . strval($member_id));
            if (!empty($_points_received)) {
                $points_received -= @intval($_points_received[0]['_amount_gift_points']) + @intval($_points_received[0]['_amount_points']);
            }
        }

        return $points_received;
    }

    /**
     * Calculate points earned to be displayed on POINTS_PROFILE.tpl.
     *
     * @param  MEMBER $member_id_of The ID of the member who is being viewed
     * @param  ?MEMBER $member_id_viewing The ID of the member who is doing the viewing (null: current member)
     * @param  array $point_info The map containing the members point info (fields as enumerated in description) from point_info()
     * @return ?array Point record for use in POINTS_PROFILE.tpl. This is its own Tempcode variable and not an item in the equation list. (null: addon disabled)
     */
    public function points_profile(int $member_id_of, ?int $member_id_viewing, array $point_info) : ?array
    {
        $points_received = array_key_exists('points_received', $point_info) ? $point_info['points_received'] : 0;

        return [
            '_POINTS_RECEIVED' => strval($points_received),
            'POINTS_RECEIVED' => integer_format($points_received, 0),
        ];
    }
}
