/**
 * Hook class.
 */
class Hook_profiles_tabs_ecommerce_logs
{
    /**
     * Find whether this hook is active.
     *
     * @param  MEMBER $member_id_of The ID of the member who is being viewed
     * @param  MEMBER $member_id_viewing The ID of the member who is doing the viewing
     * @return boolean Whether this hook is active
     */
    public function is_active($member_id_of, $member_id_viewing)
    {
        if (!addon_installed('ecommerce')) {
            return false;
        }

        return has_actual_page_access($member_id_of, 'purchase') && (($member_id_of == $member_id_viewing) || (has_actual_page_access($member_id_viewing, 'admin_ecommerce_logs')));
    }

    /**
     * Render function for profile tab hooks.
     *
     * @param  MEMBER $member_id_of The ID of the member who is being viewed
     * @param  MEMBER $member_id_viewing The ID of the member who is doing the viewing
     * @param  boolean $leave_to_ajax_if_possible Whether to leave the tab contents null, if this hook supports it, so that AJAX can load it later
     * @return array A tuple: The tab title, the tab contents, the suggested tab order, the icon
     */
    public function render_tab($member_id_of, $member_id_viewing, $leave_to_ajax_if_possible = false)
    {
        require_lang('ecommerce');

        $title = do_lang_tempcode('PURCHASES');

        $order = 50;

        if ($leave_to_ajax_if_possible) {
            return array($title, null, $order, 'menu/rich_content/ecommerce/purchase');
        }

        $sales_table = new Tempcode();
        $pagination = new Tempcode();

        require_code('ecommerce_logs');
        $tmp = build_sales_table($member_id_viewing, false, false, 20, true);

        if ($tmp !== null) {
            $sales_table = $tmp[0];
            $pagination = $tmp[1];
        }

        $content = do_template('CNS_MEMBER_PROFILE_ECOMMERCE_LOGS', array('_GUID' => 'e490d230b9521415616be0c610434d93', 'CONTENT' => $sales_table, 'PAGINATION' => $pagination));

        return array($title, $content, $order, 'menu/rich_content/ecommerce/purchase');
    }
}
