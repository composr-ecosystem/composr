    /**
     * Get written context for an action log entry handled by this hook.
     *
     * @param  array $actionlog_row Action log row
     * @param  array $handler_data Handler data
     * @param  ?string $identifier Identifier (null: none)
     * @return string Written context
     */
    protected function get_written_context($actionlog_row, $handler_data, $identifier)
    {
        switch ($actionlog_row['the_type']) {
            case 'ADD_WARNING':
            case 'EDIT_WARNING':
            case 'DELETE_WARNING':
                $written_context = $GLOBALS['FORUM_DRIVER']->get_username(intval($actionlog_row['param_b']));
                if ($written_context === null) {
                    return '#' . $actionlog_row['param_b'];
                }
                return $written_context;

            case 'SILENCE_FROM_FORUM':
            case 'UNSILENCE_FORUM':
                $username = $GLOBALS['FORUM_DRIVER']->get_username(intval($actionlog_row['param_a']));
                if ($username === null) {
                    $username = '#' . $actionlog_row['param_b'];
                }

                $forum_name = $GLOBALS['FORUM_DB']->query_select_value_if_there('f_forums', 'f_name', ['id' => intval($actionlog_row['param_b'])]);
                if ($forum_name === null) {
                    $forum_name = '#' . $actionlog_row['param_b'];
                }

                $written_context = do_lang('SOMETHING_TO', $username, $forum_name);
                return $written_context;

            case 'SILENCE_FROM_TOPIC':
            case 'UNSILENCE_TOPIC':
                $username = $GLOBALS['FORUM_DRIVER']->get_username(intval($actionlog_row['param_a']));
                if ($username === null) {
                    $username = '#' . $actionlog_row['param_b'];
                }

                $topic_title = $GLOBALS['FORUM_DB']->query_select_value_if_there('f_topics', 't_cache_first_title', ['id' => intval($actionlog_row['param_b'])]);
                if ($topic_title === null) {
                    $topic_title = '#' . $actionlog_row['param_b'];
                }

                $written_context = do_lang('SOMETHING_TO', $username, $topic_title);
                return $written_context;
        }

        return parent::get_written_context($actionlog_row, $handler_data, $identifier);
    }
