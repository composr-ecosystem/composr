/**
 * Hook class.
 */
class Hook_actionlog_wiki extends Hook_actionlog
{
    /**
     * Get details of action log entry types handled by this hook.
     *
     * @return array Map of handler data in standard format
     */
    public function get_handlers()
    {
        if (!addon_installed('wiki')) {
            return array();
        }

        require_lang('wiki');

        return array(
            'WIKI_ADD_PAGE' => array(
                'flags' => ACTIONLOG_FLAGS_NONE,
                'cma_hook' => 'wiki_page',
                'identifier_index' => 0,
                'written_context_index' => 1,
                'followup_page_links' => array(
                    '_WIKI_PAGE' => '_SEARCH:wiki:browse:{ID}',
                ),
            ),
            'WIKI_EDIT_PAGE' => array(
                'flags' => ACTIONLOG_FLAGS_NONE,
                'cma_hook' => 'wiki_page',
                'identifier_index' => 0,
                'written_context_index' => 1,
                'followup_page_links' => array(
                    '_WIKI_PAGE' => '_SEARCH:wiki:browse:{ID}',
                ),
            ),
            'WIKI_DELETE_PAGE' => array(
                'flags' => ACTIONLOG_FLAGS_NONE,
                'cma_hook' => 'wiki_page',
                'identifier_index' => 0,
                'written_context_index' => 1,
                'followup_page_links' => array(
                    'WIKI_HOME' => '_SEARCH:wiki',
                ),
            ),
            'WIKI_EDIT_TREE' => array(
                'flags' => ACTIONLOG_FLAGS_NONE,
                'cma_hook' => 'wiki_page',
                'identifier_index' => 0,
                'written_context_index' => 1,
                'followup_page_links' => array(
                    '_WIKI_PAGE' => '_SEARCH:wiki:browse:{ID}',
                ),
            ),
            'WIKI_MAKE_POST' => array(
                'flags' => ACTIONLOG_FLAGS_NONE,
                'cma_hook' => 'wiki_post',
                'identifier_index' => 0,
                'written_context_index' => null,
                'followup_page_links' => array(
                    '_WIKI_PAGE' => '_SEARCH:wiki:browse:{1}',
                    'WIKI_EDIT_POST' => '_SEARCH:wiki:post:post_id={ID}',
                ),
            ),
            'WIKI_EDIT_POST' => array(
                'flags' => ACTIONLOG_FLAGS_NONE,
                'cma_hook' => 'wiki_post',
                'identifier_index' => 0,
                'written_context_index' => null,
                'followup_page_links' => array(
                    '_WIKI_PAGE' => '_SEARCH:wiki:browse:{1}',
                    'WIKI_EDIT_POST' => '_SEARCH:wiki:post:post_id={ID}',
                ),
            ),
            'WIKI_DELETE_POST' => array(
                'flags' => ACTIONLOG_FLAGS_NONE,
                'cma_hook' => 'wiki_post',
                'identifier_index' => 0,
                'written_context_index' => null,
                'followup_page_links' => array(
                    '_WIKI_PAGE' => '_SEARCH:wiki:browse:{1}',
                ),
            ),
            'MERGE_WIKI_POSTS' => array(
                'flags' => ACTIONLOG_FLAGS_NONE,
                'cma_hook' => 'wiki_page',
                'identifier_index' => 0,
                'written_context_index' => 1,
                'followup_page_links' => array(
                    '_WIKI_PAGE' => '_SEARCH:wiki:browse:{ID}',
                ),
            ),
            'WIKI_MOVE_POST' => array(
                'flags' => ACTIONLOG_FLAGS_NONE,
                'cma_hook' => 'wiki_post',
                'identifier_index' => 0,
                'written_context_index' => null,
                'followup_page_links' => array(
                    '_WIKI_PAGE' => '_SEARCH:wiki:browse:{1}',
                    'WIKI_EDIT_POST' => '_SEARCH:wiki:post:post_id={ID}',
                ),
            ),
        );
    }

    /**
     * Get written context for an action log entry handled by this hook.
     *
     * @param  array $actionlog_row Action log row
     * @param  array $handler_data Handler data
     * @param  ?string $identifier Identifier (null: none)
     * @return string Written context
     */
    protected function get_written_context($actionlog_row, $handler_data, $identifier)
    {
        switch ($actionlog_row['the_type']) {
            case 'WIKI_MAKE_POST':
            case 'WIKI_DELETE_POST':
                $_page_title = $GLOBALS['SITE_DB']->query_select_value_if_there('wiki_pages', 'title', array('id' => intval($actionlog_row['param_b'])));
                if ($_page_title === null) {
                    $page_title = '#' . $actionlog_row['param_b'];
                } else {
                    $page_title = get_translated_text($_page_title);
                }

                $written_context = do_lang('SOMETHING_IN', '#' . $actionlog_row['param_a'], $page_title);
                return $written_context;

            case 'WIKI_EDIT_POST':
                $member_id = $GLOBALS['SITE_DB']->query_select_value_if_there('wiki_posts', 'member_id', array('id' => intval($actionlog_row['param_a'])));
                if ($member_id !== null) {
                    $username = $GLOBALS['FORUM_DRIVER']->get_username($member_id);
                    if ($username === null) {
                        $username = '#' . strval($member_id);
                    }
                } else {
                    $username = do_lang('UNKNOWN');
                }

                $_page_title = $GLOBALS['SITE_DB']->query_select_value_if_there('wiki_pages', 'title', array('id' => intval($actionlog_row['param_b'])));
                if ($_page_title === null) {
                    $page_title = '#' . $actionlog_row['param_b'];
                } else {
                    $page_title = get_translated_text($_page_title);
                }

                require_lang('wiki');
                $written_context = do_lang('POST_BY_X_IN_Y', $username, $page_title);
                return $written_context;

            case 'WIKI_MOVE_POST':
                $_page_title = $GLOBALS['SITE_DB']->query_select_value_if_there('wiki_pages', 'title', array('id' => intval($actionlog_row['param_b'])));
                if ($_page_title === null) {
                    $page_title = '#' . $actionlog_row['param_a'];
                } else {
                    $page_title = get_translated_text($_page_title);
                }

                $written_context = do_lang('SOMETHING_TO', '#' . $actionlog_row['param_a'], $page_title);
                return $written_context;
        }

        return parent::get_written_context($actionlog_row, $handler_data, $identifier);
    }
}
