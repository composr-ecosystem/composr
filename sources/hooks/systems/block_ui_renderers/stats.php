/**
 * Hook class.
 */
class Hook_block_ui_renderers_stats
{
    /**
     * See if a particular block parameter's UI input can be rendered by this.
     *
     * @param  ID_TEXT $block The block
     * @param  ID_TEXT $parameter The parameter of the block
     * @param  boolean $has_default Whether there is a default value for the field, due to this being an edit
     * @param  string $default Default value for field
     * @param  Tempcode $description Field description
     * @return ?Tempcode Rendered field (null: not handled)
     */
    public function render_block_ui($block, $parameter, $has_default, $default, $description)
    {
        if (!addon_installed('stats')) {
            return null;
        }

        if ($block . ':' . $parameter == 'main_staff_stats_graph:param') { // special case for stats graphs
            require_code('stats');
            $list = new Tempcode();
            $rows = $GLOBALS['SITE_DB']->query_select('poll', ['id', 'question'], [], 'ORDER BY add_time DESC,id DESC', 100/*In case insane number*/);
            foreach (stats_find_graphs_in_category() as $graph_name => $_) {
                list(, $graph_details) = $_;
                $list->attach(form_input_list_entry($graph_name, $has_default && $graph_name == $default, $graph_details['label']));
            }
            return form_input_list(do_lang_tempcode('BLOCK_main_staff_stats_graph_PARAM_param_TITLE'), escape_html($description), $parameter, $list, null, false, false);
        }
        return null;
    }
}
