/**
 * Hook class.
 */
class Hook_commandr_fs_extended_member__notifications_enabled
{
    /**
     * Whether the filesystem hook is active.
     *
     * @return boolean Whether it is
     */
    public function is_active() : bool
    {
        return true;
    }

    /**
     * Read a virtual property for a member file.
     *
     * @param  MEMBER $member_id The member ID
     * @return mixed The data
     */
    public function read_property(int $member_id)
    {
        return table_to_portable_rows('notifications_enabled', ['id'], ['l_member_id' => $member_id]);
    }

    /**
     * Read a virtual property for a member file.
     *
     * @param  MEMBER $member_id The member ID
     * @param  mixed $data The data
     */
    public function write_property(int $member_id, $data)
    {
        table_from_portable_rows('notifications_enabled', $data, ['l_member_id' => $member_id], TABLE_REPLACE_MODE_BY_EXTRA_FIELD_DATA);
    }
}
