/**
 * Hook class.
 */
class Hook_task_export_quiz
{
    /**
     * Run the task hook.
     *
     * @param  AUTO_LINK $quiz_id The quiz ID
     * @return ?array A tuple of at least 2: Return mime-type, content (either Tempcode, or a string, or a filename and file-path pair to a temporary file), map of HTTP headers if transferring immediately, map of ini_set commands if transferring immediately (null: show standard success message)
     */
    public function run($quiz_id)
    {
        if (!addon_installed('quizzes')) {
            return null;
        }

        $filename = 'quiz.csv';

        $headers = array();
        $headers['Content-type'] = 'text/csv';
        $headers['Content-Disposition'] = 'attachment; filename="' . escape_header($filename) . '"';

        $ini_set = array();
        $ini_set['ocproducts.xss_detect'] = '0';

        require_code('files2');

        $outfile_path = cms_tempnam();

        $data = get_quiz_data_for_csv($quiz_id);
        make_csv($data, $filename, false, false, $outfile_path);

        return array('text/csv', array($filename, $outfile_path), $headers, $ini_set);
    }
}
