/**
 * Hook class.
 */
class Hook_cleanup_download_galleries
{
    /**
     * Find details about this cleanup hook.
     *
     * @return ?array Map of cleanup hook info (null: hook is disabled)
     */
    public function info() : ?array
    {
        if (!addon_installed('downloads')) {
            return null;
        }
        if (!addon_installed('galleries')) {
            return null;
        }

        require_lang('galleries');

        $info = [];
        $info['title'] = do_lang_tempcode('PRUNE_DOWNLOAD_GALLERIES');
        $info['description'] = do_lang_tempcode('DESCRIPTION_PRUNE_DOWNLOAD_GALLERIES');
        $info['type'] = 'optimise';

        return $info;
    }

    /**
     * Run the cleanup hook action.
     *
     * @return Tempcode Results
     */
    public function run() : object
    {
        if (!addon_installed('downloads')) {
            return new Tempcode();
        }
        if (!addon_installed('galleries')) {
            return new Tempcode();
        }

        require_code('galleries2');

        $rows = $GLOBALS['SITE_DB']->query_select('galleries', ['name'], [], ' AND name LIKE \'' . db_encode_like('download_%') . '\'');

        foreach ($rows as $row) {
            $download_id = intval(str_replace('download_', '', $row['name']));

            $download_exists = $GLOBALS['SITE_DB']->query_select_value_if_there('download_downloads', 'id', ['id' => $download_id]);
            if ($download_exists === null) {
                delete_gallery($row['name']);
            }
        }

        return new Tempcode();
    }
}
