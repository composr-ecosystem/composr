/**
 * Hook class.
 */
class Hook_trusted_sites_ecommerce
{
    /**
     * Detect what needs to be 'added' to the trusted_sites_1 option.
     *
     * @param  array $sites List of trusted sites (written by reference)
     */
    public function find_trusted_sites_1(array &$sites)
    {
    }

    /**
     * Detect what needs to be 'added' to the trusted_sites_2 option.
     *
     * @param  array $sites List of trusted sites (written by reference)
     */
    public function find_trusted_sites_2(array &$sites)
    {
        if (!addon_installed('ecommerce')) {
            return;
        }

        require_code('ecommerce');

        if ((get_option('payment_gateway') == 'paypal') && (get_ecommerce_option('payment_gateway_username') != '')) {
            $sites[] = 'paypal.com';
            $sites[] = 'ipnpb.paypal.com';
            $sites[] = 'sandbox.paypal.com';
            $sites[] = 'ipnpb.sandbox.paypal.com';
        }

        if ((get_option('payment_gateway') == 'authorize') && (get_ecommerce_option('payment_gateway_username') != '')) {
            $sites[] = 'authorize.net';
            $sites[] = 'test.authorize.net';
        }
    }
}
