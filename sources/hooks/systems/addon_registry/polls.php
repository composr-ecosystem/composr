    public function poll(string $section = '') : array
    {
        $tpl = new Tempcode();
        switch ($section) {
            case 'poll':
                foreach (placeholder_array() as $k => $v) {
                    $tpl->attach(do_lorem_template('POLL_ANSWER', [
                        'PID' => placeholder_id(),
                        'I' => strval($k),
                        'CAST' => strval($k),
                        'VOTE_URL' => placeholder_url(),
                        'ANSWER' => lorem_phrase(),
                        'ANSWER_PLAIN' => lorem_phrase(),
                    ]));
                }
                break;

            case 'result':
                foreach (placeholder_array() as $k => $v) {
                    $tpl->attach(do_lorem_template('POLL_ANSWER_RESULT', [
                        'PID' => placeholder_id(),
                        'I' => strval($k),
                        'VOTE_URL' => placeholder_url(),
                        'ANSWER' => lorem_phrase(),
                        'ANSWER_PLAIN' => lorem_phrase(),
                        'WIDTH' => strval($k),
                        'VOTES' => placeholder_number(),
                        '_VOTES' => placeholder_number(),
                        'TOTAL_VOTES' => placeholder_number(),
                    ]));
                }
                break;

            default:
                foreach (placeholder_array() as $k => $v) {
                    $tpl->attach(do_lorem_template('POLL_ANSWER', [
                        'PID' => placeholder_id(),
                        'I' => strval($k),
                        'CAST' => strval($k),
                        'VOTE_URL' => placeholder_url(),
                        'ANSWER' => lorem_phrase(),
                        'ANSWER_PLAIN' => lorem_phrase(),
                    ]));
                }
                foreach (placeholder_array() as $k => $v) {
                    $tpl->attach(do_lorem_template('POLL_ANSWER_RESULT', [
                        'PID' => placeholder_id(),
                        'I' => strval($k),
                        'VOTE_URL' => placeholder_url(),
                        'ANSWER' => lorem_phrase(),
                        'ANSWER_PLAIN' => lorem_phrase(),
                        'WIDTH' => strval($k),
                        'VOTES' => placeholder_number(),
                        '_VOTES' => placeholder_number(),
                        'TOTAL_VOTES' => placeholder_number(),
                    ]));
                }
        }

        $wrap_content = do_lorem_template('POLL_BOX', [
            '_GUID' => '4c6b026f7ed96f0b5b8408eb5e5affb5',
            'VOTE_URL' => placeholder_url(),
            'GIVE_CONTEXT' => true,
            'SUBMITTER' => placeholder_id(),
            'RESULT_URL' => placeholder_url(),
            'SUBMIT_URL' => placeholder_url(),
            'ARCHIVE_URL' => placeholder_url(),
            'PID' => placeholder_id(),
            'COMMENT_COUNT' => placeholder_number(),
            'QUESTION_PLAIN' => lorem_phrase(),
            'QUESTION' => lorem_phrase(),
            'CONTENT' => $tpl,
            'FULL_URL' => placeholder_url(),
            'TOTAL_VOTES' => placeholder_number(),
        ]);

        return [
            lorem_globalise($wrap_content, null, '', true)
        ];
    }

