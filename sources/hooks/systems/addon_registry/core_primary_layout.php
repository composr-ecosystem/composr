    /**
     * Get an e-mail template preview.
     *
     * @param  ID_TEXT $tpl Template name
     * @return Tempcode Preview
     */
    protected function do_mail_template_preview(string $tpl) : object
    {
        $message_raw = comcode_to_tempcode(do_lang('NEW_COMMENT_BODY', lorem_phrase(), lorem_phrase(), [lorem_phrase(), lorem_paragraph(), static_evaluate_tempcode(placeholder_url()), lorem_phrase(), placeholder_codename(), lorem_phrase()]));

        require_css('email');

        $map = [
            'LOGOURL' => placeholder_image_url(),
            'LOGOMAP' => '',
            'LANG' => fallback_lang(),
            'TITLE' => lorem_phrase(),
            'CONTENT' => $message_raw,
            'CSS' => '{CSS}',
        ];

        $_tpl = do_lorem_template($tpl, $map);

        $css = css_tempcode(true, false, $_tpl->evaluate());
        $_css = $css->evaluate();

        return make_string_tempcode(str_replace('{CSS}', $_css, $_tpl->evaluate()));
    }

