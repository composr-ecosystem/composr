    {
        $automatic = [];
        foreach (placeholder_array() as $k => $v) {
            $_content = do_lorem_template('NEWSLETTER_WHATSNEW_RESOURCE_FCOMCODE', [
                'MEMBER_ID' => placeholder_numeric_id(),
                'URL' => placeholder_url(),
                'NAME' => lorem_word(),
                'DESCRIPTION' => lorem_paragraph(),
                'IMAGE_URL' => placeholder_image_url(),
                'CONTENT_TYPE' => lorem_word(),
                'CONTENT_ID' => placeholder_codename(),
                'CATALOGUE' => null,
            ], null, false, null, '.txt', 'text');

            $tmp = do_lorem_template('NEWSLETTER_WHATSNEW_SECTION_FCOMCODE', [
                'I' => '1',
                'TITLE' => lorem_phrase(),
                'CONTENT' => $_content,
                'IMAGE_URL' => placeholder_image_url(),
            ], null, false, null, '.txt', 'text');
            $automatic[] = $tmp->evaluate();
        }

        $content = '';
        foreach ($automatic as $tp) {
            $content .= $tp;
        }

        return lorem_globalise(do_lorem_template('NEWSLETTER_WHATSNEW_FCOMCODE', [
            'CONTENT' => $content,
        ], null, false, null, '.txt', 'text'), null, '', true);
    }

