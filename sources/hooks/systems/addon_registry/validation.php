/**
 * Hook class.
 */
class Hook_addon_registry_validation
{
    /**
     * Get a list of file permissions to set.
     *
     * @param  boolean $runtime Whether to include wildcards represented runtime-created chmoddable files
     * @return array File permissions to set
     */
    public function get_chmod_array(bool $runtime = false) : array
    {
        return [];
    }

    /**
     * Get the current version of this addon (usually software major, software minor, addon build).
     * Put the comment "// addon_version_auto_update" to the right of the return if you want release tools to automatically update this according to software version and find_addon_effective_md5.
     *
     * @return SHORT_TEXT Version number
     */
    public function get_version() : string
    {
        return '11.0.1'; // addon_version_auto_update 44868d9b8b4b7ecabbaaed6460942b35
    }

    /**
     * Get the minimum required version of the website software needed to use this addon.
     *
     * @return float Minimum required website software version
     */
    public function get_min_cms_version() : float
    {
        return 11.0;
    }

    /**
     * Get the maximum compatible version of the website software to use this addon.
     *
     * @return ?float Maximum compatible website software version (null: no maximum version currently)
     */
    public function get_max_cms_version() : ?float
    {
        return 11.9;
    }

    /**
     * Get the addon category.
     *
     * @return string The category
     */
    public function get_category() : string
    {
        return 'Community';
    }

    /**
     * Get the description of the addon.
     *
     * @return string Description of the addon
     */
    public function get_description() : string
    {
        return "This addon enables the ability to set content as validated (public) or not (staff / direct link only), and to subject content submissions by members to approval (validation) by staff. Additionally, if you have Commandr installed, you can schedule content to automatically validate at a given date and time.";
    }

    /**
     * Get a list of tutorials that apply to this addon.
     *
     * @return array List of tutorials
     */
    public function get_applicable_tutorials() : array
    {
        return [
            'tut_censor',
        ];
    }

    /**
     * Get a mapping of dependency types.
     *
     * @return array A structure specifying dependency information
     */
    public function get_dependencies() : array
    {
        return [
            'requires' => [],
            'recommends' => ['commandr', 'System scheduler'],
            'conflicts_with' => [],
            'previously_in_addon' => ['core_unvalidated', 'unvalidated'],
        ];
    }

    /**
     * Explicitly say which icon should be used.
     *
     * @return URLPATH Icon
     */
    public function get_default_icon() : string
    {
        return 'themes/default/images/icons/menu/adminzone/audit/not_validated.svg';
    }

    /**
     * Get a list of files that belong to this addon.
     *
     * @return array List of files
     */
    public function get_file_list() : array
    {
        return [
            'adminzone/pages/modules/admin_validation.php',
            'lang/EN/validation.ini',
            'sources/hooks/blocks/main_staff_checklist/validation.php',
            'sources/hooks/systems/addon_registry/validation.php',
            'sources/hooks/systems/config/validate_new_comcode_pages.php',
            'sources/hooks/systems/notifications/content_validated.php',
            'sources/hooks/systems/notifications/needs_validation.php',
            'sources/hooks/systems/page_groupings/validation.php',
            'sources/hooks/systems/validation/.htaccess',
            'sources/hooks/systems/validation/index.html',
            'sources/validation.php',
            'sources_custom/hooks/systems/validation/.htaccess',
            'sources_custom/hooks/systems/validation/index.html',
            'themes/default/images/icons/menu/adminzone/audit/not_validated.svg',
            'themes/default/images/icons_monochrome/menu/adminzone/audit/not_validated.svg',
            'themes/default/templates/VALIDATION_SCREEN.tpl',
            'themes/default/templates/VALIDATION_SECTION.tpl',
            'themes/default/text/VALIDATION_REQUEST_MAIL.txt',
        ];
    }

    /**
     * Get mapping between template names and the method of this class that can render a preview of them.
     *
     * @return array The mapping
     */
    public function tpl_previews() : array
    {
        return [
            'templates/VALIDATION_SECTION.tpl' => 'administrative__validation_screen',
            'templates/VALIDATION_SCREEN.tpl' => 'administrative__validation_screen',
            'text/VALIDATION_REQUEST_MAIL.txt' => 'administrative__validation_request_mail',
        ];
    }

    /**
     * Get a preview(s) of a (group of) template(s), as a full standalone piece of HTML in Tempcode format.
     * Uses sources/lorem.php functions to place appropriate stock-text. Should not hard-code things, as the code is intended to be declarative.
     * Assumptions: You can assume all Lang/CSS/JavaScript files in this addon have been pre-required.
     *
     * @return Tempcode Preview
     */
    public function tpl_preview__administrative__validation_request_mail() : object
    {
        return lorem_globalise(do_lorem_template('VALIDATION_REQUEST_MAIL', [
            'USERNAME' => lorem_word(),
            'CONTENT_TYPE_LABEL' => lorem_phrase(),
            'TITLE' => lorem_phrase(),
            'TYPE' => lorem_phrase(),
            'ID' => placeholder_codename(),
            'URL' => placeholder_url(),
        ], null, false, null, '.txt', 'text'), null, '', true);
    }

    /**
     * Get a preview(s) of a (group of) template(s), as a full standalone piece of HTML in Tempcode format.
     * Uses sources/lorem.php functions to place appropriate stock-text. Should not hard-code things, as the code is intended to be declarative.
     * Assumptions: You can assume all Lang/CSS/JavaScript files in this addon have been pre-required.
     *
     * @return Tempcode Preview
     */
    public function tpl_preview__administrative__validation_screen() : object
    {
        $section = do_lorem_template('VALIDATION_SECTION', [
            'TITLE' => lorem_phrase(),
            'CONTENT' => lorem_phrase(),
        ]);
        return lorem_globalise(do_lorem_template('VALIDATION_SCREEN', [
            'TITLE' => lorem_screen_title(),
            'SECTIONS' => $section,
            'TEXT' => lorem_paragraph_html(),
        ]), null, '', true);
    }
}
