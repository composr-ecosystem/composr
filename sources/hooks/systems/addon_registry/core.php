                'CONTENT_TIME' => placeholder_date(),
                '_CONTENT_TIME' => placeholder_date_raw(),
            ],
        ];

        if ($render_mode != 'table') {
            $content_data[0]['CONTENT_KEYMAP'] = $keymap;
