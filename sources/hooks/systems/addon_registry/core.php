                break;

            case 'grid':
                $content[] = '';
                break;

            case 'list':
                $content[] = placeholder_link();
                break;

            case 'mosaic':
                $content[] = '';
                break;

            case 'slider':
                $content[] = '';
                break;

            case 'table':
                $content = [['KEYMAP' => $keymap]];
                break;

            case 'tiles':
                $content[] = '';
                break;
