            'themes/default/templates/FILEDUMP_SEARCH.tpl',
            'uploads/filedump/.htaccess',
            'uploads/filedump/index.html',
        ];
    }

    /**
     * Get mapping between template names and the method of this class that can render a preview of them.
     *
     * @return array The mapping
     */
    public function tpl_previews() : array
    {
        return [
            'templates/FILEDUMP_SCREEN.tpl' => 'filedump_screen',
            'templates/FILEDUMP_FOOTER.tpl' => 'filedump_screen',
            'templates/FILEDUMP_SEARCH.tpl' => 'filedump_screen',
            'templates/FILEDUMP_EMBED_SCREEN.tpl' => 'filedump_embed_screen',
        ];
    }

    /**
     * Get a preview(s) of a (group of) template(s), as a full standalone piece of HTML in Tempcode format.
     * Uses sources/lorem.php functions to place appropriate stock-text. Should not hard-code things, as the code is intended to be declarative.
     * Assumptions: You can assume all Lang/CSS/JavaScript files in this addon have been pre-required.
     *
     * @return Tempcode Preview
     */
    public function tpl_preview__filedump_screen() : object
    {
        require_css('forms');

        $thumbnails = [];
        $thumbnails[] = [
            'FILENAME' => lorem_word(),
            'SUBPATH' => placeholder_codename(),
            'IMAGE_URL' => placeholder_image_url(),
            'IS_IMAGE' => true,
            'URL' => placeholder_url(),
            'DESCRIPTION' => lorem_paragraph(),
            'ACTIONS' => lorem_paragraph(),
            '_SIZE' => placeholder_number(),
            'SIZE' => placeholder_number(),
            '_TIME' => placeholder_date_raw(),
            'DATE' => placeholder_date(),
            'WIDTH' => placeholder_number(),
            'HEIGHT' => placeholder_number(),
            'IS_DIRECTORY' => false,
            'CHOOSABLE' => false,
            'EMBED_URL' => placeholder_url(),
        ];

        return lorem_globalise(do_lorem_template('FILEDUMP_SCREEN', [
            'TITLE' => lorem_screen_title(),
            'SUBPATH' => placeholder_codename(),
            'THUMBNAILS' => $thumbnails,
            'LISTING' => placeholder_table(),
            'UPLOAD_FORM' => placeholder_form(),
            'CREATE_FOLDER_FORM' => placeholder_form(),
            'TYPE_FILTER' => '',
            'SEARCH' => '',
            'SORT' => 'time ASC',
            'PAGINATION_LISTING' => placeholder_pagination(),
            'PAGINATION_THUMBNAILS' => placeholder_pagination(),
            'POST_URL' => placeholder_url(),
            'DIRECTORIES' => [lorem_word()],
            'OTHER_DIRECTORIES' => [lorem_word()],
            'FILTERED_DIRECTORIES' => [lorem_word()],
            'FILTERED_DIRECTORIES_MISSES' => [],
            'SOMETHING_EDITABLE' => true,
        ]), null, '', true);
    }

    /**
     * Get a preview(s) of a (group of) template(s), as a full standalone piece of HTML in Tempcode format.
     * Uses sources/lorem.php functions to place appropriate stock-text. Should not hard-code things, as the code is intended to be declarative.
     * Assumptions: You can assume all Lang/CSS/JavaScript files in this addon have been pre-required.
     *
     * @return Tempcode Preview
     */
    public function tpl_preview__filedump_embed_screen() : object
    {
        $image_sizes = [];
        foreach (placeholder_array() as $k => $v) {
            $image_sizes[] = [
                'LABEL' => $v,
                'SIZE_URL' => placeholder_image_url(),
                'SIZE_WIDTH' => placeholder_number() . strval($k),
                'SIZE_HEIGHT' => placeholder_number() . strval($k),
            ];
        }

        return lorem_globalise(do_lorem_template('FILEDUMP_EMBED_SCREEN', [
            'TITLE' => lorem_screen_title(),
            'FORM' => placeholder_form(),
            'IMAGE_SIZES' => $image_sizes,
            'URL' => placeholder_image_url(),
            'EXISTING_COUNT' => placeholder_number(),
            'DESCRIPTION' => lorem_phrase(),
            'IMAGE' => true,
        ]), null, '', true);
    }
}
