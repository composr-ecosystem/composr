    {
        $urls = [];
        foreach (placeholder_array() as $v) {
            $urls[] = [
                'URL' => placeholder_url(),
                'PATH' => lorem_phrase(),
            ];
        }

        $message = do_lorem_template('CLEANUP_ORPHANED_UPLOADS', [
            'FOUND' => $urls,
        ]);

        return [
            lorem_globalise(do_lorem_template('CLEANUP_COMPLETED_SCREEN', [
                'TITLE' => lorem_title(),
                'MESSAGES' => $message,
            ]), null, '', true)
        ];
    }

