        $results = new Tempcode();
        foreach (placeholder_array() as $k => $v) {
            $results->attach(do_lorem_template('POINTS_SEARCH_RESULT', [
                'URL' => placeholder_url(),
                'ID' => placeholder_id(),
                'USERNAME' => lorem_word(),
