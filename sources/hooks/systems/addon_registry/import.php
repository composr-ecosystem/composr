    {
        $extra = do_lorem_template('IMPORT_MESSAGE', [
            'MESSAGE' => lorem_phrase(),
        ]);
        $import_list = do_lorem_template('IMPORT_ACTION_LINE', [
            'CHECKED' => false,
            'DISABLED' => true,
            'NAME' => lorem_word(),
            'TEXT' => lorem_phrase(),
            'ADVANCED_URL' => placeholder_url(),
        ]);

