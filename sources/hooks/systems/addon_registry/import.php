            'EXTRA' => $extra,
            'MESSAGE' => lorem_phrase(),
            'TITLE' => lorem_screen_title(),
            'FIELDS' => lorem_phrase(),
            'HIDDEN' => '',
            'IMPORTER' => lorem_phrase(),
            'IMPORT_LIST' => $import_list,
