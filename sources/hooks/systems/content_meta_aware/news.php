    {
        if (!addon_installed('news')) {
            return null;
        }

        return [
            'support_custom_fields' => true,
            'content_type_label' => 'news:NEWS_ARTICLE',
            'content_type_universal_label' => 'News article',

            'db' => $GLOBALS['SITE_DB'],
            'table' => 'news',
            'id_field' => 'id',
            'id_field_numeric' => true,
            'parent_category_field' => 'news_category',
            'parent_category_meta_aware_type' => 'news_category',
            'is_category' => false,
            'is_entry' => true,
            'category_field' => 'news_category', // For category permissions
            'permission_module' => 'news', // For category permissions
            'parent_spec__table_name' => 'news_categories',
            'parent_spec__parent_name' => null,
            'parent_spec__field_name' => 'id',
            'category_is_string' => false,

            'title_field' => 'title',
            'title_field_dereference' => true,
            'title_field_supports_comcode' => true,
            'description_field' => 'news',
            'description_field_dereference' => true,
            'description_field_supports_comcode' => true,
            'image_field' => ['news_image_url', 'news_category', 'CALL: generate_news_image_url'],
            'image_field_is_theme_image' => false,
            'alternate_icon_theme_image' => null,

            'view_page_link_pattern' => '_SEARCH:news:view:_WILD',
            'edit_page_link_pattern' => '_SEARCH:cms_news:_edit:_WILD',
            'view_category_page_link_pattern' => '_SEARCH:news:browse:_WILD',
            'add_url' => ($get_extended_data && has_submit_permission('mid', get_member(), get_ip_address(), 'cms_news')) ? (get_module_zone('cms_news') . ':cms_news:add') : null,
            'archive_url' => $get_extended_data ? ((($zone !== null) ? $zone : get_module_zone('news')) . ':news') : null,

            'support_url_monikers' => true,

            'views_field' => 'news_views',
            'order_field' => null,
            'submitter_field' => 'submitter',
            'author_field' => 'author',
            'add_time_field' => 'date_and_time',
            'edit_time_field' => 'edit_date',
            'date_field' => 'date_and_time',
            'validated_field' => 'validated',
            'additional_sort_fields' => null,

            'seo_type_code' => 'news',

            'feedback_type_code' => 'news',

            'search_hook' => 'news',
            'rss_hook' => 'news',
            'attachment_hook' => 'news',
            'notification_hook' => 'news_entry',
            'sitemap_hook' => 'news',

            'addon_name' => 'news',

            'cms_page' => 'cms_news',
            'module' => 'news',

            'commandr_filesystem_hook' => 'news',
            'commandr_filesystem__is_folder' => false,

