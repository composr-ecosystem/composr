/**
 * Hook class.
 */
class Hook_privacy_content_privacy extends Hook_privacy_base
{
    /**
     * Find privacy details.
     *
     * @return ?array A map of privacy details in a standardised format (null: disabled)
     */
    public function info() : ?array
    {
        if (!addon_installed('content_privacy')) {
            return null;
        }
        if (get_forum_type() != 'cns') {
            return null;
        }

        require_lang('content_privacy');

        return [
            'cookies' => [
            ],

            'positive' => [
                [
                    'heading' => do_lang('INFORMATION_STORAGE'),
                    'explanation' => do_lang_tempcode('PRIVACY_SETTINGS_EXPLANATION', escape_html(static_evaluate_tempcode(build_url(['page' => 'members', 'type' => 'view'], get_module_zone('members'), [], false, false, false, 'tab__edit__privacy')))),
                ],
            ],

            'general' => [
            ],

            'database_records' => [
                'content_privacy__members' => [
                    'timestamp_field' => null,
                    'retention_days' => null,
                    'retention_handle_method' => PRIVACY_METHOD__LEAVE,
                    'member_id_fields' => ['member_id'],
                    'ip_address_fields' => [],
                    'email_fields' => [],
                    'additional_anonymise_fields' => [],
                    'extra_where' => null,
                    'removal_default_handle_method' => PRIVACY_METHOD__DELETE,
                    'allowed_handle_methods' => PRIVACY_METHOD__DELETE,
                ],
            ],
        ];
    }

    /**
     * Serialise a row.
     *
     * @param  ID_TEXT $table_name Table name
     * @param  array $row Row raw from the database
     * @return array Row in a cleanly serialised format
     */
    public function serialise(string $table_name, array $row) : array
    {
        $ret = parent::serialise($table_name, $row);

        switch ($table_name) {
            case 'content_privacy__members':
                require_code('content');
                list($title) = content_get_details($row['content_type'], $row['content_id']);
                $ret += [
                    'content_title__dereferenced' => $title,
                ];
                break;
        }

        return $ret;
    }
}
