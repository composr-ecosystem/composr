/**
 * Hook class.
 */
class Hook_privacy_newsletter extends Hook_privacy_base
{
    /**
     * Find privacy details.
     *
     * @return ?array A map of privacy details in a standardised format (null: disabled)
     */
    public function info()
    {
        if (!addon_installed('newsletter')) {
            return null;
        }

        return array(
            'cookies' => array(
            ),

            'positive' => array(
            ),

            'general' => array(
            ),

            'database_records' => array(
                'newsletter_subscribe' => array(
                    'timestamp_field' => null,
                    'retention_days' => null,
                    'retention_handle_method' => PRIVACY_METHOD_leave,
                    'member_id_fields' => array(),
                    'ip_address_fields' => array(),
                    'email_fields' => array('email'),
                    'additional_anonymise_fields' => array(),
                    'extra_where' => null,
                    'removal_default_handle_method' => PRIVACY_METHOD_delete,
                ),
                'newsletter_subscribers' => array(
                    'timestamp_field' => 'join_time',
                    'retention_days' => null,
                    'retention_handle_method' => PRIVACY_METHOD_leave,
                    'member_id_fields' => array(),
                    'ip_address_fields' => array(),
                    'email_fields' => array('email'),
                    'additional_anonymise_fields' => array('n_forename', 'n_surname'),
                    'extra_where' => null,
                    'removal_default_handle_method' => PRIVACY_METHOD_delete,
                ),
                'newsletter_drip_send' => array(
                    'timestamp_field' => null,
                    'retention_days' => null,
                    'retention_handle_method' => PRIVACY_METHOD_leave,
                    'member_id_fields' => array(),
                    'ip_address_fields' => array(),
                    'email_fields' => array('d_to_email'),
                    'additional_anonymise_fields' => array('d_to_name'),
                    'extra_where' => null,
                    'removal_default_handle_method' => PRIVACY_METHOD_delete,
                ),
            ),
        );
    }

    /**
     * Serialise a row.
     *
     * @param ID_TEXT Table name
     * @param array Row raw from the database
     * @return array Row in a cleanly serialised format
     */
    public function serialise($table_name, $row)
    {
        $ret = serialise($table_name, $row);

        switch ($table_name) {
            case 'newsletter_subscribe':
                $ret += array(
                    'newsletter_id__dereferenced' => get_translated_text($GLOBALS['SITE_DB']->query_select_value('newsletters', 'title', array('id' => $row['newsletter_id']))),
                );
                break;
        }

        return $ret;
    }

    /**
     * Delete a row.
     *
     * @param ID_TEXT Table name
     * @param array Row raw from the database
     */
    public function delete($table_name, $row)
    {
        switch ($table_name) {
            case 'newsletter_subscriber':
                $GLOBALS['SITE_DB']->query_delete('newsletter_subscribe', array('email' => $row['email']));
                $this->delete($table_name, $row);
                break;

            default:
                $this->delete($table_name, $row);
                break;
        }
    }
}
