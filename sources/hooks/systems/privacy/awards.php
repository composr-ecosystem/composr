/**
 * Hook class.
 */
class Hook_privacy_awards extends Hook_privacy_base
{
    /**
     * Find privacy details.
     *
     * @return ?array A map of privacy details in a standardised format (null: disabled)
     */
    public function info() : ?array
    {
        if (!addon_installed('awards')) {
            return null;
        }

        return [
            'label' => 'awards:AWARDS',

            'description' => 'awards:DESCRIPTION_PRIVACY_AWARDS',

            'cookies' => [
            ],

            'positive' => [
            ],

            'general' => [
            ],

            'database_records' => [
                'award_archive' => [
                    'timestamp_field' => 'date_and_time',
                    'retention_days' => null,
                    'retention_handle_method' => PRIVACY_METHOD__LEAVE,
                    'owner_id_field' => 'member_id',
                    'additional_member_id_fields' => [],
                    'ip_address_fields' => [],
                    'email_fields' => [],
                    'username_fields' => [],
                    'additional_anonymise_fields' => [],
                    'extra_where' => null,
                    'removal_default_handle_method' => PRIVACY_METHOD__DELETE,
                    'allowed_handle_methods' => PRIVACY_METHOD__ANONYMISE | PRIVACY_METHOD__DELETE,
                ],
            ],
        ];
    }

    /**
     * Serialise a row.
     *
     * @param  ID_TEXT $table_name Table name
     * @param  array $row Row raw from the database
     * @return array Row in a cleanly serialised format
     */
    public function serialise(string $table_name, array $row) : array
    {
        $ret = parent::serialise($table_name, $row);

        switch ($table_name) {
            case 'award_archive':
                $content_type = $GLOBALS['SITE_DB']->query_select_value_if_there('award_types', 'a_content_type', ['id' => $row['a_type_id']]);
                if ($content_type !== null) {
                    require_code('content');
                    list($title, , $info) = content_get_details($content_type, $row['content_id']);
                    $ret += [
                        'content_type__dereferenced' => do_lang($info['content_type_label']),
                        'content_title__dereferenced' => $title,
                    ];
                }
                break;
        }

        return $ret;
    }
}
