/**
 * Hook class.
 */
class Hook_privacy_filedump extends Hook_privacy_base
{
    /**
     * Find privacy details.
     *
     * @return ?array A map of privacy details in a standardised format (null: disabled)
     */
    public function info()
    {
        if (!addon_installed('filedump')) {
            return null;
        }

        return array(
            'cookies' => array(
            ),

            'positive' => array(
            ),

            'general' => array(
            ),

            'database_records' => array(
                'filedump' => array(
                    'timestamp_field' => null,
                    'retention_days' => null,
                    'retention_handle_method' => PRIVACY_METHOD_leave,
                    'member_id_fields' => array('the_member'),
                    'ip_address_fields' => array(),
                    'email_fields' => array(),
                    'additional_anonymise_fields' => array(),
                    'extra_where' => null,
                    'removal_default_handle_method' => PRIVACY_METHOD_anonymise,
                ),
            ),
        );
    }

    /**
     * Delete a row.
     *
     * @param  ID_TEXT $table_name Table name
     * @param  array $row Row raw from the database
     */
    public function delete($table_name, $row)
    {
        switch ($table_name) {
            case 'filedump':
                $rows = $GLOBALS['SITE_DB']->query_select('filedump', array('*'), array('id' => $row['id']), '', 1);
                if (array_key_exists(0, $rows)) {
                    $path = get_custom_file_base() . '/uploads/filedump' . $row['subpath'] . $row['name'];
                    @unlink($path);
                    sync_file($path);
                    $GLOBALS['SITE_DB']->query_delete('filedump', array('id' => $row['id']));
                }
                break;

            default:
                $this->delete($table_name, $row);
                break;
        }
    }
}
