/**
 * Hook class.
 */
class Hook_gu_downloads
{
    /**
     * Add in new icons to a worked-within gallery.
     *
     * @param  ID_TEXT $cat Gallery name
     * @return array Results
     */
    public function new_donext_icons(string $cat) : array
    {
        if (!addon_installed('downloads')) {
            return [];
        }

        if (substr($cat, 0, 9) != 'download_') {
            return [];
        }

        $id = intval(substr($cat, 9));
        return [
            ['menu/rich_content/downloads', ['downloads', ['type' => 'entry', 'id' => $id], get_module_zone('downloads')], do_lang('VIEW')],
        ];
    }
}
