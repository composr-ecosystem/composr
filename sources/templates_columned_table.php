/*
Columned table = Simple low-assumption table with data organised into columns
*/

/**
 * Get the Tempcode for a table header row.
 *
 * @param  array $values The array of field titles that define the entries in the table table
 * @return Tempcode The generated header
 */
function columned_table_header_row(array $values) : object
{
    $cells = new Tempcode();
    foreach ($values as $value) {
        $cells->attach(do_template('COLUMNED_TABLE_HEADER_ROW_CELL', ['_GUID' => '5002f54ccddf7259f3460d8c0759fd1a', 'VALUE' => $value]));
    }

    return do_template('COLUMNED_TABLE_HEADER_ROW', ['_GUID' => '2f4095b8d30f50f34fdd6acf8dd566b1', 'CELLS' => $cells]);
}

/**
 * Get the Tempcode for a table row.
 *
 * @param  array $values The array of values that make up this row
 * @param  boolean $escape Whether to add escaping
 * @return Tempcode The generated row
 */
function columned_table_row(array $values, bool $escape) : object
{
    $cells = new Tempcode();
    foreach ($values as $value) {
        if (($escape) && (!is_object($value))) {
            $value = make_string_tempcode(escape_html(is_object($value) ? $value->evaluate() : $value));
        }

        $cells->attach(do_template('COLUMNED_TABLE_ROW_CELL', ['_GUID' => '700a982eb2262149295816ddee91b0e7', 'VALUE' => $value]));
    }

    return do_template('COLUMNED_TABLE_ROW', ['_GUID' => 'a4efacc07ecb165e37c355559f476ae9', 'CELLS' => $cells]);
}

/**
 * Get the Tempcode for a cell containing a member.
 *
 * @param  AUTO_LINK $member_id The ID of the member being added to the cell
 * @return Tempcode A Tempcode containing the member, a hyperlink to their profile, and a member info tooltip
 */
function columned_table_member_cell(int $member_id) : object
{
    $username = $GLOBALS['FORUM_DRIVER']->get_username($member_id, false, USERNAME_DEFAULT_NULL);
    if ($username === null) {
        return do_lang_tempcode('UNKNOWN');
    }

    $profile_url = $GLOBALS['FORUM_DRIVER']->member_profile_url($member_id, true, $username);

    return do_template('COLUMNED_TABLE_MEMBER_CELL', [
        '_GUID' => '289bcaf692854b588f9b1eda3c0c52f4',
        'PROFILE_URL' => $profile_url,
        'USERNAME' => $username,
        'MEMBER_ID' => strval($member_id)
    ]);
}
