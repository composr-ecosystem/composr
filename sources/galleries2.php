        }
        if (($title == '') && (get_option('gallery_media_title_required') != '0')) {
            warn_exit(do_lang_tempcode('NO_PARAMETER_SENT', 'title'));
        }

        return [
            STRING_MAGIC_NULL,
            STRING_MAGIC_NULL,
            $title,
            STRING_MAGIC_NULL,
        ];
    }

    require_code('uploads');

    if ($url == '') {
        $_filename = '';
        require_code('images2');
        $_url = post_param_image('image', 'uploads/galleries', null, false, $is_edit, $_filename);
        if ($url == '') {
            if ($_url != '') {
                $url = $_url;
            }
        }
        if ($url == '') {
            warn_exit(do_lang_tempcode('IMPROPERLY_FILLED_IN_UPLOAD'));
        }
        if ($filename === null) {
            if ($_filename != '') {
                $filename = $_filename;
            }
        }
    }

    if ($title == '') {
        $title = post_param_string('title', '');
    }

    if ($cat == '') {
        $cat = post_param_string('cat', '');
    }

    list(
        $title,
