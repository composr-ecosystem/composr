/**
 * Work out stats of what domains are used.
 * Returns in reverse count order.
 *
 * @param  string $key The newsletter identifier (as expected in $send_details parameter of newsletter_who_send_to)
 * @return array A map between domains and counts
 */
function newsletter_domain_subscriber_stats(string $key) : array
{
    $domains = [];

    if (substr($key, 0, 1) == 'g') {
        $sql_expr = db_function('SUBSTR', ['m_email_address', db_function('INSTR', ['m_email_address', '\'@\''])]);
        $rows = $GLOBALS['FORUM_DB']->query_select('f_members', [$sql_expr . ' AS email', 'COUNT(*) as cnt'], ['m_allow_emails' => 1, 'm_primary_group' => intval(substr($key, 1))], 'GROUP BY ' . $sql_expr);
    } elseif ($key == '-1') {
        $sql_expr = db_function('SUBSTR', ['m_email_address', db_function('INSTR', ['m_email_address', '\'@\''])]);
        $rows = $GLOBALS['FORUM_DB']->query_select('f_members', [$sql_expr . ' AS email', 'COUNT(*) as cnt'], ['m_allow_emails' => 1], 'GROUP BY ' . $sql_expr);
    } else {
        $sql_expr = db_function('SUBSTR', ['email', db_function('INSTR', ['email', '\'@\''])]);
        $rows = $GLOBALS['SITE_DB']->query_select('newsletter_subscribe', [$sql_expr . ' AS email', 'COUNT(*) as cnt'], [], 'GROUP BY ' . $sql_expr);
    }
    foreach ($rows as $row) {
        $email = $row['email'];
        if (strpos($email, '@') === false) {
            continue;
        }
        $domain = substr($email, strpos($email, '@') + 1);
        if (!is_string($domain)) {
            continue;
        }
        $cnt = array_key_exists('cnt', $row) ? $row['cnt'] : 1;
        if (!array_key_exists($domain, $domains)) {
            $domains[$domain] = 0;
        }
        $domains[$domain] += $cnt;
    }

    arsort($domains);

    return $domains;
}

/**
 * Work out newsletter block list.
 *
 * @return array List of blocked e-mail addresses (actually a map)
 */
function newsletter_block_list() : array
{
    $blocked = [];
    $block_path = get_custom_file_base() . '/uploads/website_specific/newsletter_blocked.csv';
    if (is_file($block_path)) {
        require_code('files_spreadsheets_read');
        $sheet_reader = spreadsheet_open_read($block_path, null, CMS_Spreadsheet_Reader::ALGORITHM_RAW);
        while (($row = $sheet_reader->read_row()) !== false) {
            if ($row[0] != '') {
                $blocked[$row[0]] = true;
            }
        }
        $sheet_reader->close();
    }
    return $blocked;
}
