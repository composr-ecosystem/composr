    if (!$GLOBALS['DEV_MODE']) {
        deldir_contents(get_custom_file_base() . '/uploads/cns_avatars', true);
        deldir_contents(get_custom_file_base() . '/uploads/cns_photos', true);
    }

    delete_attachments('cns_post');
    delete_attachments('cns_signature');

    require_all_core_cms_code();

    $tables = [
        'f_emoticons',
        'f_custom_fields',
        'f_member_custom_fields',
        'f_groups',
        'f_forum_groupings',
        'f_forums',
        'f_forum_intro_ip',
        'f_forum_intro_member',
        'f_topics',
        'f_posts',
        'f_polls',
        'f_poll_answers',
        'f_poll_votes',
        'f_post_templates',
        'f_warnings',
        'f_moderator_logs',
        'f_member_known_login_ips',
        'f_members',
        'f_group_members',
        'f_group_approvals',
        'f_read_logs',
        'f_forum_tracking',
        'f_topic_tracking',
        'f_multi_moderations',
        'f_invites',
        'f_forum_group_access',
        'f_special_pt_access',
        'f_saved_warnings',
        'f_member_cpf_perms',
        'f_group_join_log',
        'f_password_history',
        'f_pposts_fulltext_index',
        'f_posts_fulltext_index',
    ];
    $GLOBALS['FORUM_DB']->drop_table_if_exists($tables);

    $GLOBALS['FORUM_DB']->query_delete('group_privileges', ['module_the_name' => 'forums']);

